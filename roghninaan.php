<h1>Ingredients Of Roghni Naan</h1>
Oven Temp: 200 C - 400 F
<br />4 Cups Refined Flour
<br />1 1/4 Cups Yogurt
<br />1 tsp Baking Soda
<br />To taste Salt
<br />For garnishing Onion seeds

</div>

<b><h1>How to Make Roghni Naan</h1></b>
1).Mix together the flour, salt and baking soda. Knead into a soft, smooth dough using as much yoghurt as required.
<br />2).Cover with a damp cloth and leave to rise in a warm, draught-free place, till double in volume. If in a hurry, place the container in a larger container of hot water.
<br />3).The time taken for the dough to rise varies according to the weather - about 3 hours in summer and 7 hours in winter.
<br />4).Punch the dough and leave to rise again. This time it will take much less time.
<br />5).Break the dough into pieces of desirable size and smooth into rounds.
<br />6).Cover with a damp cloth and keep for at least 15 minutes.
<br />7).Roll the balls into flat ovals or rounds. You can also flatten them by slapping them between your palm - as experts do, stretching and pulling with your hands when required.
<br />8).Smear the surface with water and sprinkle with onion seeds.
<br />9).Grease a baking tray and bake the naans in a pre-heated oven for about 5 minutes.
<br />10).Better still is to grill them, if your grill has elements both above and below. If you have a tandoor, wet one side of the naan with water and stick it to the walls of the tandoor.
<br />11).You can also stick it to a hot griddle and turn the griddle upside down over the flame. The naan will fall off when cooked.
<br />12).Brush the hot naan with butter or ghee and serve.
