<div class="container">
<div class="image"><img src="tandoorichicken.jpeg" height="400px" width="800px" /></div>
<div class="description">Next on the list is that famous Indian barbecue chicken recipe. It's hard to find anyone who doesn't fall in love with tandoori chicken. Serve this one over basmati rice tossed with turmeric and add a few slices of fresh onion for an extra kick.

Once you have the basic recipe for tandoori masala, prepare and store it so you can cook tandoori chicken anytime you feel the urge. Trust us, this will become your family's new favorite chicken dinner.
