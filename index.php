<div class="container">
<div class="image"><img src="roganjosh.jpg" height="400px" width="800px" /></div>
<div class="description">The literal translation for the name of this dish from Kashmir is "red lamb." The color comes from Kashmiri dry red chilies. While the name may sound fiery, the heat of the dish is toned down by the cream that is added at the end.

A trip to your local international food market should set you up with things like cardamom, cumin, turmeric, and garam masala. They're often sold in small bulk quantities, so they'll last for quite a long time.
