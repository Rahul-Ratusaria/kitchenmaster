<div class="container">
<div class="image"><img src="chole.jpg" height="400px" width="800px" /></div>
<div class="description">A classic and easy chickpea curry, chole is a favorite in Northern India and has become a worldwide sensation. It's perfect for a crowd, especially if you serve it hot along with fried Indian leavened bread like poori or bhatura.

Again, the ingredient list looks more intimidating than the recipe is in reality. Pick up some chickpeas, onions, tomatoes, along with garlic and ginger pastes. From there it's just those common Indian spices that make this food unique.
