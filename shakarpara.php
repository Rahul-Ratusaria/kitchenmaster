<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Shakar Para</h1>
1/4 cup sugar
<br />1/4 cup milk
<br />2 tbsp ghee
<br />1 1/2 cups whole wheat flour (gehun ka atta)
<br />a pinch of salt
<br />ghee for deep-frying
</div>

<b><h1>How to Make Shakar Para</h1></b>
1).Combine the milk, sugar and ghee in a deep non-stick pan, mix well and cook on a medium flame for 2 minutes or till the sugar dissolves, while stirring occasionally. Keep aside to cool.
<br />2).Sieve the whole wheat flour and salt together in a deep bowl with the help of a strainer.
<br />3).Add the milk-sugar mixture, a little at a time and knead into a firm dough.
<br />4).Divide the dough into 4 equal portions.
<br />5).Roll out a portion into 175mm. (7) diameter circle.
<br />6).Cut it into 25 mm. (1) diamond shaped pieces and prick each diamond with a fork.
<br />7).Heat the ghee in a deep non-stick kadhai and deep-fry the shakarparas, a few at a time on a slow flame till they are golden brown in colour from both the sides. Drain on an absorbent paper.
<br />8).Repeat steps 5 to 7 to make more shakarparas in 3 more batches.
<br />9).Cool completely and store in an air-tight container.

