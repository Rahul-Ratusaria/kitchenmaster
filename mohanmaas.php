<h3 style="padding-top:20px; margin:10px; color:#0000cc;">Relish the creamy texture, nutty aroma and subtle spices of this authentic Rajasthani meat curry. Meat is boiled and cooked with masalas, almond-cashew paste and a generous helping of cream, milk and curd.
