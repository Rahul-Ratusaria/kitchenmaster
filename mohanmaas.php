<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients of Mohan Maas</h1>
200 gm meat
<br />2 Tbsp clarified butter
<br />2 cinnamon sticks
<br />1/4 tsp cumin seeds
<br />1 cassia leaf
<br />4 cardamom
<br />4 Tbsp of onion paste
<br />2 tsp ginger garlic paste
<br />2 Tbsp almond-cashew nut paste
<br />2 tsp yoghurt
<br />2 Tbsp milk
<br />1 tsp cream
<br />Salt to taste
</div>

<b><h1>How to Make Mohan Maas</h1></b>
1).Grind together onions to a fine paste.
<br />2).Add salt.
<br />3).Blanch almonds and cashew nuts.
<br />4).Grind them to make a smooth paste.
<br /><b>5).For the main preparation:</b>
<br />6).Partially boil the meat and keep aside.
<br />7).Heat clarified butter in pan.
<br />8).Add cinnamon, cumin seeds, cassia leaf and cardamom.
<br />9).Cook till the cumin seeds start crackling.
<br />10).Then add onion paste, ginger garlic paste, salt, semi boiled meat, almond and cashew nut paste, curd, milk and cream.
<br />11).Mix well.
<br />12).Garnish with cream and coriander leaves.
<br />13).Serve hot with rice.

