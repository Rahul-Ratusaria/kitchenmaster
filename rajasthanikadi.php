<h1>Ingredients Of Rajasthani Kadi</h1>
1/2 Kg Potatoes (peeled& mashed smooth), boiled
<br />2 tsp Rock salt
<br />1/4 tsp Chilli powder
<br />1/2 Cup Singhare ka atta
<br /><b>For deep-frying Oil</b>
<br />1/2 Cup Sour yogurt
<br />1 sprig Curry leaf
<br />1/2 tsp Cumin seeds
<br />2 Whole red chillies
<br />1 Tbsp Ginger, chopped
<br />1/2 tsp Coriander powder
<br />4 Cups Water
<br />For garnishing Coriander leaves
</div>

<b><h1>How to Make Rajasthani Kadi</h1></b>
1).Mix the potatoes, 1/2 teaspoon salt, chilli powder and singhare ka atta into a thick batter.
<br />2).Put aside 1/4 of the mixture and the rest will be made into pakoris.
<br />3).Heat oil till a drop of batter dropped in comes up at once.
<br />4).Drop spoonfuls into oil, lower the flame and fry to a golden colour.
<br />5).Drain on an absorbent paper and keep aside.
<br />6).Add the yogurt to the remaining mixture, make a smooth paste and mix in water.
<br />7).In a heavy-based pan, reheat 2 tbsp of the same oil, add curry leaves, cumin seeds and whole red chillies.
<br />8).When slightly darkened, add ginger and sautï¿½ a little.
<br />9).Add the yogurt mixture, salt and coriander powder and bring to a boil and then simmer over low flame till it thickens a bit; stirring frequently to avoid scorching.
<br />10).Add the pakoris, simmer a couple of minutes more and serve hot, garnished with coriander leaves.

