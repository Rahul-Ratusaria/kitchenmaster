<h1>Ingredients Of Ker Sangri</h1>
<b>For ker sangri:</b>
<br />75 Gram Berries
<br />75 Gram Dried beans
<br /><b>For main preparation:</b>
<br />2 tsp Oil
<br />1 tsp Cumin seeds
<br />2 tsp Ginger garlic paste
<br />5-6 Whole red chillies
<br />1 tsp Coriander powder
<br />1 tsp Red chilli powder
<br />1 tsp Turmeric powder
<br />1 tsp Mango powder
<br />5-6 Dried pieces of mango
<br />4 tsp Yogurt
<br />1 tsp Salt
<br /><b>For Garnish:</b>
<br />Fried garlic
<br />Fried Red chillies (whole)
<br />Coriander leaves
</div>

<b><h1>How to Make Ker Sangri</h1></b>
<br /><b>To prepare ker sangri:</b>
<br />1).Soak the berries and the dried beans in beaten yogurt and keep it overnight.
<br />2).Boil for 15-20 min in water and clean them properly with water.
<br /><b>To prepare the main preparation:</b>
<br />1).Heat oil in pan.
<br />2).Add cumin seeds, ginger-garlic paste, whole red chillies, coriander powder, red chilli powder, turmeric powder, mango powder, dried mango pieces and curd.
<br />3).Saute the masala well.
<br />4).Then add ker sangri and salt. Mix it well.
<br />5).Garnish with fried garlic, fried whole red chillies and coriander leaves.
<br />6).Serve the Ker Sangri hot with roti.

