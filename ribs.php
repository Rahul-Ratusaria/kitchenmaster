<h1>Ingredients Of Kashmiri Style Mutton Ribs</h1>
500 gms lamb ribs; cut into 2-3 pieces
<br />2 inch cinnamon bark
<br />3 black cardamom
<br />5 green cardamom
<br />2 dried bay leaf
<br />1 tbsp cumin seeds
<br />1 tbsp crushed fennel seeds
<br />½ tbsp dried ginger powder
<br />A pinch of asafoetida/hing
<br />2 large garlic cloves; crushed
<br />1 tsp turmeric powder
<br />Salt, to season
<br />1 cup milk
<br />2-3 tbsp ghee/clarified butter

</div>

<b><h1>How to Make Kashmiri Style Mutton Ribs</h1></b>
1).In a heavy bottomed vessel, add the lamb ribs and fill with water, enough to just cover the ribs.
<br />2).Bring to boil and remove the scum that floats on the surface.
<br />3).Then add all the spices, garlic and season generously with salt. Also add 1 cup milk and stir well to combine.
<br />4).Cover the vessel, reduce the flame and slow cook the ribs for 1 hour or till the meat has become tender and almost fall off the bone.
<br />5).Remove from heat and take out the ribs slowly and keep aside. You can either keep the ribs in large chunks or cut into smaller pieces.
<br />6).Heat another flat pan, add the ghee and add the ribs one by one. Fry on medium to high heat till one side has caramelized before turning over. Remove when the other side has also caramelized well.
<br />7).Serve warm.
