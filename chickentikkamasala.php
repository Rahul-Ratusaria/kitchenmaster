<b><h1>Ingredients</h1></b>
<br />CHICKEN
<br />2 chicken breasts, cut into one inch cubes
<br />1 cup plain greek yogurt
<br />1 lemon, juiced
<br />2 teaspoons ground cumin
<br />1 teaspoon cayenne pepper
<br />1 teaspoon black pepper
<br />1 teaspoon ground cinnamon
<br />1 teaspoon salt
<br />1 teaspoon ground ginger
<br />SAUCE
<br />1 tablespoon butter
<br />2 cloves garlic, minced
<br />1 jalapeno, minced
<br />1 teaspoon ground cumin
<br />2 teaspoon paprika
<br />1 teaspoon gharam masala
<br />1 (16 oz) can tomato sauce
<br />ï¿½ teaspoon salt
<br />1 cup heavy whipping cream
<br />cilantro for garnish
</div>

<b><h1>How To Make Chicken Tikka Masala</h1></b>
<b>For the chicken:</b>
<br />In a medium-sized bowl, combine the greek yogurt, lemon juice, cumin, cayenne pepper, pepper, cinnamon, salt, ginger, and cubed chicken. Stir until well combined and chicken is coated. Let marinate for at least an hour.
<br />Heat a large skillet over medium-high heat. Cook chicken, stirring occasionally, until cooked through, about 5 to 7 minutes.
<br />Remove the chicken from the pan and wipe the pan out before making the sauce.
<b><h1>For sauce:</h1></b>
Melt the butter in a medium sauce pan. Add in the garlic and jalapeno and saute for 1-2 minutes.
<br />Add in the cumin, paprika, and gharam masala and toast for another minute. Add in the tomato sauce, season with salt and bring to a simmer.
<br />Stir in the whipping cream and cooked chicken and cook until heated through, about 2 minutes.
<br />Serve hot over rice.

