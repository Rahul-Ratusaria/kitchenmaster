<h1>Ingredients Of Laal Maas</h1>
Meat, cut into curry style pieces 500 g, from the Raan
<br />2 Tbsp garlic paste
<br />6-7 sabut lal mirch, soaked in water for 2 hours and ground fine
<br />1 1/2 tsp salt
<br />1/2 cup ghee
<br />8 cloves / laung
<br />8 green elaichi
<br />1/8 tsp dal chini, powdered
<br />8 sabut kali mirch
<br />A pan 8
</div>

<b><h1>How to Make Laal Maas</h1></b>
1).Marinate meat in lassan (garlic), salt and laal mirch (red chilli) paste for 3-4 hours.
<br />2).Heat the ghee in a pan and add the laung, elaichi, dal chini and kali mirch. Stir once then add the meat mixture over high heat.
<br />3).Saute till opaque, then lower the heat and cook covered at the lowest heat till tender and fat separates.

