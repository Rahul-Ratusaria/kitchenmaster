<h1>Ingredients Of Kashmiri Saag</h1>
7 Tbsp mustard oil
<br />3 brown cardamom
<br />10 kashmiri whole chillies
<br />25 whole garlic
<br />250 gms whole spinach
<br />Salt for taste
</div>

<b><h1>How to Make Kashmiri Saag</h1></b>
1).In a pressure cooker add mustard oil, brown cardamom, kashmiri whole chillies, whole garlic, whole spinach (saag), little salt and water.
<br />2).Put all of the above in a pressure cooker and let the spinach (saag) cook till 1 whistle.
<br />3).Serve the Kashmiri Saag steaming hot.


