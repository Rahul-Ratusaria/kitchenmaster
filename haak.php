<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Haak</h1>
1/2 kg haak
<br />1/2 cup of water
<br />3-4 tbs mustard oil
<br />1 tsp chopped garlic
<br />1/4 tsp heeng
<br />5-6 whole red chillies
<br />2-3 brown cardamom
<br />1/2 tsp dry ginger powder
<br />1 tsp saunf
<br />Salt - to taste

</div>

<b><h1>How to Make Haak</h1></b>
1).In a deep vessel, heat the mustard oil to smoking point.
<br />2).Add the garlic, heeng, red chillies and cardamom and stir.
<br />3).Add the ginger powder, saunf and salt, stir again.
<br />4).Now add the haak mix well and cook till it reduces a little.
<br />5).Pour in the water, cover and cook for another 10-15 minutes.
<br />6).Haak Saag is ready to serve.

