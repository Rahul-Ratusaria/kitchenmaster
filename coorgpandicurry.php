<h3 style="padding-top:20px; margin:10px; color:#0000cc;">A famous recipe from Karnataka. Pork is considered to be a delicacy in Coorg. The meat is cooked in a freshly made masala paste which lends it its rich colour. A delicious, spicy pork curry to make for a dinner party.
