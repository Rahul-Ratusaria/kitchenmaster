<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Coorg Pandi Curry</h1>
<b>Masalas:</b>
<br />1 tsp Fenugreek seeds
<br />1/2 tsp Cumin seeds
<br />1/2 tsp Coriander seeds
<br />1 tsp Pepper
<br /><b>For the masala paste:</b>
<br />2 1/2 Small Onions (quartered)
<br />1/2 Ginger
<br />7 Cloves
<br />10-12 Curry leaves
<br />4-5 Small Green chillies
<br />Handful Coriander leaves
<br />1/2 tsp Oil
<br /><b>Other ingredients:</b>
<br />1/2 Kg Pork
<br />To taste Salt
<br />1 tsp Oil
<br />1/4 tsp Haldi
<br />1/2 tsp Red chilli powder
<br />1/4 tsp Mustard seeds
<br />6-7 Curry leaves
<br />1 Tbsp Masala
<br />1/2 Cup Pork stock
<br />1 Tbsp Masala paste
<br />1 tsp Turmeric extract
<br />4-5 Cups Water
</div>

<b><h1>How to Make Coorg Pandi Curry</h1></b>
1).Take a pot, add 1/2 kg pork, water, salt and let it simmer for 40 minutes.
<br />2).In the meanwhile prepare the masala.
<br /><b>Prepare the masala:</b>
<br />1).Take a pan, add fenugreek seeds, cumin seeds, coriander seeds and pepper. Mix well. Roast it till it changes color. Once roasted, powder the mix and use it as a masala. Keep aside.
<br /><b>Prepare the masala paste:</b>
<br />1).Take a pan, add onions, ginger,cloves, curry leaves and small green chillies.
<br />2).Mix well. Let the onions turn brown in color.
<br />3).Once cooked, add a handful of coriander leaves and oil so that the ingredients don't stick to each other.
<br />4).Mix and remove from heat. Now grind this paste nicely. Keep aside.
<br /><b>Final Preparation:</b>
<br />1).Now take a pan, add oil, mustard seeds, curry leaves, masala paste and mix well.
<br />2).Then add half a cup of water, masala, half cup pork stock, haldi and red chilli powder.
<br />3).Once fried, add pork, turmeric extract and salt to taste. Mix well.
<br />4).Add 4-5 cups of water. Cover the pan and cook it till the pork is nice and tender.
<br />5).Make sure that the pork is coated well with the masala.
<br />6).Once cooked garnish it with coriander leaves.
<br />7).Serve hot with steamed rice.

