<h1>Ingredients Of Gujarati Samosa</h1>
<br />1/2 Kg Flour dough (Samosa dough)
<br /><b>For filling:</b>
<br />2 tsp Oil
<br />1/2 Cup Green peas (grounded)
<br />1/2 tsp Sugar
<br />1/2 tsp Lemon juice
<br />1 tsp Coriander, chopped
<br />To taste Salt

</div>

<b><h1>How to Make Gujarati Samosa</h1></b>
1).Make thin roundels from the flour dough using rolling pin and keep aside.
<br /><b>Prepare the filling:</b>
<br />1).Heat oil in a pan and saute green peas.
<br />2).Add sugar, lemon juice, salt and coriander. Cook for few minutes.
<br />3).Now fold the roundels in the shape of a cone.
<br />4).Fill them with the green peas filling and seal them with little water.
<br />5).Fry the samosa cones till they turn color and serve with chutney.



