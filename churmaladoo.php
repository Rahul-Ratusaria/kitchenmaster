<h3 style="padding-top:20px; margin:10px; color:#0000cc;">Choorma is a flour based traditional Rajasthani sweet. Ladoos made with whole wheat flour, gond and almonds.
</h2>
<div>

<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Choorma Ladoo</h1>
1/4 cup ghee
<br />250 gm atta
<br />60 gm gond
<br />120 gm sugar-powdered
<br />1/2 cup almonds-roasted and pounded coarsely
<br />1 tsp cardamom powder
<br />Ghee to fry
</div>

<b><h1>How to Make Choorma Ladoo</h1></b>
1).Rub the 1/2 cup ghee into atta then knead to a stiff dough.
<br />2).Shape dough into flat rounds (need not be very smooth) and fry first over high heat, then low, till brown and cooked through and crisp.
<br />3).When cool, powder in a grinder or by pounding, till a fine powder (some like to sieve it).
<br />4).Fry the gond over low heat till cooked through (it should cut through easily).
<br />5).Remove from ghee and grind while still hot.
<br />6).Mix into the cooled powder, rubbing it well into the powder.
<br />7).Mix the sugar, almonds and cardamom into the powder and shape into laddoos, or serve as a powder.
<br />8).Note: Some people like to strain the powder through a fine sieve, before mixing the sugar and cardamoms.

