<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Paneer Chaman</h1>
7 Tbsp Mustard oil
<br />250 Gram Paneer
<br />3 Brown cardamom
<br />5 Green cardamom
<br />1 Bay leaf
<br />1 Cinnamon stick
<br />1 1/2 Cup Tomato puree
<br />A pinch of Heeng
<br />1/4 tsp Dry ginger powder
<br />To taste Salt
<br />1 tsp Red chilli powder
<br />1 Tbsp Saunf powder (aniseed powder)
<br />1 Cup Water

</div>

<b><h1>How to Make Paneer Chaman</h1></b>
1).Cut the paneer into thick rectangular shaped pieces.
<br />2).In a wok add 5 Tbsp mustard oil. Shallow fry paneer pieces from both sides till golden-brown.
<br />3).Transfer them in a bowl with 1 cup water. Add a pinch of heeng.
<br />4).Ina wok add 2 Tbsp oil. Splutter brown cardamom, green cardamom, bay leaf, cinnamon.
<br />5).Add tomato puree, salt, red chilli powder, dry ginger powder. Cook till the masala leaves oil on the sides.
<br />6).Soak out the extra water from the paneer. Add it to the masala. Now add some saunf powder. Mix.
<br />7).Now add the water in which the paneer was soaked.
<br />8).Mix well. Let it cook for 1 more minute.
<br />9).Serve with steamed rice.


