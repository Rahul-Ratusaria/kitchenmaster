<h1>Ingredients Of Murg Malai Kebab</h1>
14 small chicken pieces
<br />1 cup sour cream (You can substitute it with hung curd)
<br />1 tsp ginger paste
<br />1 tsp garlic paste
<br />A pinch of nutmeg powder
<br />1 tsp green cardamom powder
<br />1/2 tsp black pepper powder
<br />2 Tbsp lemon juice
<br />1 cup cream cheese
<br />2 Tbsp grated mozzarella cheese
<br />1 Tbsp cornflour
<br />Salt to taste
<br />Oil to cook
</div>

<b><h1>How to Make Murg Malai Kebab</h1></b>
1).Preheat the oven at 180 degree Celsius. Place chicken pieces in a bowl.
<br />2).Add ginger paste, garlic paste, nutmeg powder, green cardamom powder, black pepper, salt and lemon juice. Mash the cream cheese and mozzarella cheese, add corn flour and mix well.
<br />3).Add the chicken and mix it well. Add sour cream and mix lightly. Now keep this to marinate for 1 hour.
<br />4).String the chicken pieces onto skewers and place them on baking tray lined with aluminum foil.
<br />5).Drizzle the remaining marinade on them and drizzle little oil over the chicken.
<br />6).Now place the tray in the preheated oven for about 20 minutes or until evenly golden.
<br />7).Transfer the chicken onto the serving plate and serve with coriander chutney.
