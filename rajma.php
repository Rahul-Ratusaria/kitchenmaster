<h3 style="padding-top:20px; margin:10px; color:#0000cc;">The good old Rajma. Red kidney beans or rajma are cooked in tomatoes, onions and spices and served hot along with plain rice or chapati. An all time favorite in a North Indian household.
</h2>
<div>

<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Rajma</h1>
<b>For the rajma mix:</b>
<br />500 Gram Rajma (red kidney beans)
<br />To taste Salt
<br />1/2 Litre Water
<br />4-5 Pieces Garlic, chopped
<br />1/2 tsp Ginger, chopped
<br />1 Whole green chilly
<br /><b>For the tempering:</b>
<br />2 Tbsp Refined oil
<br />1/2 Tbsp Cumin seeds
<br />1/2 Tbsp Cloves
<br />2 Brown cardamom
<br />1 Onion, chopped
<br />5 cloves Garlic, chopped
<br />1 tsp Ginger, chopped
<br />1/2 tsp Turmeric powder
<br />1 tsp Red chilli powder
<br />1 tsp Coriander powder
<br />1 tsp Cumin powder
<br />1/2 tsp Salt
<br />2 Tomatoes, chopped
<br />A pinch of Sugar
<br />Few Coriander leaves
<br />1 Green chilly, finely chopped
<br />1 Tbsp Butter
<br />3 Tbsp Curd
<br />1/2 Lime juice
</div>

<b><h1>How to Make Rajma</h1></b>
1).Soak the rajma overnight and pressure cook it.
<br />2).In a kadhai, mix the cooked rajma, half a litre water, chopped garlic, chopped ginger, whole green chilly and little bit of salt for taste. Let it boil.
<br />3).While the rajma is boiling, heat some oil in another pan.
<br />4).Add to this cumin seeds, cloves and brown cardamom. Saute, crackle and fry these spices to release their flavors.
<br />5).Now add the chopped onion. Saute till the onions turn brownish.
<br />6).Add to this the chopped ginger and chopped garlic. Saute them all together.
<br />7).Now add the aromatics i.e turmeric powder, red chilli powder, coriander powder, cumin powder and little salt for taste.
<br />8).Turn up the heat and dry roast all the powdered spices. Mix them all well.
<br />9).Add a little bit of water to this onion-spice mix to bhuno the spices.
<br />10).Let the pan dry up of all the water you added. When the pan screams out for more moisture that's when you add the tomatoes.
<br />11).Add the tomatoes. Add a pinch of sugar to balance the acidity, to bring out the flavor of tomatoes and also to aid caramelization of the aromatics.
<br />12).Mix the onions, tomatoes and spices well. Add some water to prevent the mix from drying out.
<br />13).Now add the green chillies and some coriander leaves.
<br />14).Mix well for a minute or two.
<br />15).Now remove the pan from the heat and add this spice mix to the kadhai in which the rajma was boiling.
<br />16).For some extra flavor add some water in which the rajma is boiling to the spice mix pan. De-glaze all the flavors and then add it back to the kadhai with rajma.
<br />17).Turn up the heat and add butter and curd. Mix them well with the rajma.
<br />18).Squeeze half a lime into the rajma.
<br />19).Cover it, lower the heat and let the rajma simmer for about 10 - 15 minutes.
<br />20).Give it a quick stir and serve the rajma hot with rice.

