<h1>Ingredients Of Beef Ularthiyathu</h1>
2 and 1/2 lb beef
<br />25 shallots (thinly sliced)
<br />2-3 green chilies (slit)
<br />1 tbsp ginger garlic paste
<br />1/4 tbsp turmeric powder
<br />1 tbsp red chili powder
<br />1 and 1/2 tbsp coriander powder
<br />2 tbsp pepper powder
<br />1 tsp meat masala
<br />1/2 tsp garam masala
<br />4-6 sprigs curry leaves
<br />1/4 cup coconut bites
<br />a pinch of mustard seeds
<br />salt (as required)
<br />oil (as required)
</div>

<b><h1>How to Make Beef Ularthiyathu</h1></b>
1).Clean and cut beef into cube shaped pieces (preferably soup cut).
<br />2).Cook the beef in a pressure cooker with salt, pepper powder, meat masala, and a little water just enough to cover the meat. Pressure cook for 2 whistles and then remove the cooker from the flame. Allow it to rest until the pressure goes off. Keep aside.
<br />3).Heat oil in a pan and splutter mustard seeds. Add curry leaves, sliced shallots, green chilies, and a little salt. Saute until the onions turn translucent.
<br />4).Add ginger garlic paste, saute for a minute until the smell resides.
<br />5).Add spice powders; chili, coriander, turmeric, garam masala, and mix well. Make sure to turn the heat to a low while adding the spice powders, so that it does not burn.
<br />6).Remove the lid from the pressure cooker and transfer the cooked beef with the remaining water to the pan, mix well with the masala and cook on a medium flame until the gravy dries out. Stir the gravy occasionally while cooking, to prevent it from sticking to the bottom of the pan. Add salt as needed.
<br />7).Meanwhile, heat a little oil in another pan and fry the coconut bites. As it starts to brown, add curry leaves and fry it along with the coconut bites until browned. Keep aside.
<br />8).When the gravy dries out, reduce the flame to a low and saute the beef in the pan for 10-15 minutes. Add the fried curry leaves and coconut bites, and mix well together. Scrape the sides of the pan, toss until the meat is brown -black and dried out completely.
<br />9).Serve hot with Rice, Porotta, Appam, Naan, or any other bread.

