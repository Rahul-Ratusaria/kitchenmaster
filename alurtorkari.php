<h1>Ingredients Of Alur Torkari</h1>
Potato 5, large
<br />Mustard oil 2 tbsp
<br />Cumin seeds 1 tsp
<br />Green chili 2, slit halfway through lengthwise
<br />Turmeric 1/4 tsp
<br />Sugar 1/2 tsp
<br />Salt to taste
</div>

<b><h1>How to Make Alur Torkari</h1></b>
1).Peel and cut the potatoes into small cubes of about 2 cm length. Wash the potato cubes and boil in salted water until 1/4th done. This would take about 10 minutes. Drain the potatoes and rinse in cold water to stop the cooking process. Set aside.
<br />2).Heat the mustard oil in a wok. Temper it with cumin seeds and slit green chilies. Let the cumin seeds crackle for few seconds and become aromatic.
<br />3).Now add the half-cooked potatoes and turmeric powder. Saut'e them on medium-low heat for 10 minutes until the potato cubes turn very light golden. (Do not fry the potatoes to dark brown, then it will loose its characteristic of simplicity.)
<br />4).Then add about 1 cup of water and bring it to boil. Season it with salt to taste and half a teaspoon of sugar.
<br />5).Cook uncovered on low until the potatoes are completely cooked and the liquid is reduced by half. The curry is not too saucy neither too dry; just adjust the consistency according to your liking.
<br />6).Remove the potato curry from heat and serve warm with luchi/poori which is the best combination with this curry; else serve it with chapatti or phulkas. Enjoy!

