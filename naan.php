<h1><b>How To Make Naan</b></h1>
1).firstly, knead the dough for 10 minutes with ingredients listed for dough.
<br />2).cover with a moist cloth and rest for 2 hours in warm place.
<br />3).after 2 hours, knead the dough slightly. to remove if any air present in the dough.
<br />4).pinch a ball sized dough.
<br />5).gently roll to oval shape using rolling pin. make sure you do not make too thin nor thick. and also keep the size in mind. it should not be larger than your tawa size.
<br />6).grease with water over naan.
<br />7).furthermore, slowly flip over and put it on hot tawa and slightly press.
<br />8).now after a minute, flip the tawa upside down and cook naan directly on flame till they turn golden brown.
<br />9).also brush with some butter mixed with coriander leaves.
<br />10).gently scrape the naan from bottom and remove.
<br />11).finally, serve butter naan hot with your favourite curry like mattar paneer.

