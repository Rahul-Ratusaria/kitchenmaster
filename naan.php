<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<b><h1>Ingredients:</h1></b>
<b>for dough:</b>
<br />2 cups maida/refined flour
<br />1 tsp sugar
<br />1 tsp baking powder
<br />� tsp baking soda
<br />salt to taste
<br />� cup curd / yogurt fresh thick
