<h1>Ingredients Of Chole Bhature</h1>
<b>For chole:</b>
<br />2 Cup Chickpeas (channas)
<br />2 tsp Oil
<br />1 Bay leaf (tej patta)
<br />1 stick Cinnamon (dalchini)
<br />3-4 Cloves (laung)
<br />1 tsp Whole pepper corns (sabut kali mirch)
<br />3 Green cardamom (choti elaichi)
<br />2 Black cardamom (badi elaichi)
<br />1 tsp Turmeric powder (haldi)
<br />1 tsp Chili powder (lal mirch powder)
<br />1 tsp Coriander powder (dhaniya powder)
<br />1 tsp Cumin powder (zeera powder)
<br />1 tsp Cumin seeds (zeera)
<br />1/2 tsp Asafoetida (heeng)
<br />To taste Salt
<br />1 Cup Onions, chopped
<br />1 Cup Tomatoes, chopped
<br />1 tsp Ginger, chopped
<br />1 tsp Garlic, chopped
<br />1 tsp Ajwain
<br />1 tsp Lime juice
<br />1 Green chili, chopped
<br />1 Tea bag
<br />1 Tbsp Butter
<br /><b>For the bhaturas:</b>
<br />2 Cups Maida (refined flour)
<br />1/2 tsp Yeast (dissolved for 10 minutes in luke warm water)
<br />1/2 Cup Whole wheat flour
<br />A pinch of Salt
<br />To knead Water
<br />For frying Oil
</div>

<b><h1>How to Make Chole Bhature</h1></b>
<h2><b>Prepare chole:</b></h2>
1).In a pan add oil, bay leaf, cinnamon, cumin seeds, cloves, whole pepper corns, green and black cardamom.
<br />2).After it gets brown add chopped onions and saute it. Now add chopped ginger and garlic.
<br />3).Followed by turmeric, chili powder, coriander powder, cumin powder, asafoetida and salt, fry the ingredients together well.
<br />4).For de glazing the pan add a little water.
<br />5).Now add the chole (soaked overnight and pressure cooked) to the masala.
<br />6).After stirring well add tomatoes, little sugar and salt to the chole.
<br />7).Now add ajwain, chopped green chilies and water for the base.
<br />8).To get the color in the chole, add a tea bag to the masala.
<br />9).Simmer the chole gently for an hour and cover it.
<br />10).Add lime juice and a dollop of butter to it.
<br />11).Garnish the chole with coriander and butter and serve them hot with bhaturas.
<b><h2>Prepare the bhaturas:</h2></b>
1).Knead the whole wheat flour, maida and salt together with adequate amount of water.
<br />2.Sprinkle the yeast on top. Leave it for 2-3 hours for the yeast to work.
<br />3.Divide it into equal portions. Roll out in an oval or round shape.
<br />4.Deep fry till golden brown, like a puri.
<br />5.Serve.
