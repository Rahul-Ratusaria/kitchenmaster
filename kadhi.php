<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Gujarati Kadhi</h1>
2 Cups Sour yogurt
<br />2 Tbsp Gram flour
<br />4 Cups Water
<br />1/4 tsp Asafoetida
<br />1 Tbsp Salt
<br />1 tsp Chilli powder
<br />1/4 tsp Cinnamon powder
<br />2-3 Tbsp Sugar
<br />2 Tbsp Oil
<br />2 tsp Mustard seeds
<br />8-10 Curry leaves
<br />3-4 Whole red chillies
<br />2 Tbsp Coriander leaves

</div>

<b><h1>How to Make Gujarati Kadhi</h1></b>
1).Whisk the yoghurt till smooth, add gram flour to it, blending well till batter becomes smooth again.
<br />2).Add asafoetida, salt, chilli powder, cinnamon powder, sugar and water. Mix very well.
<br />3).In a heavy-based saucepan, heat oil, add mustard seeds, curry leaves and whole red chillies.
<br />4).When the seeds begin to splutter, add the yoghurt mixture, increase flame and bring to a boil.
<br />5).Lower flame and leave to simmer for about 15-20 minutes.
<br />6).Serve Gujarati Kadhi hot, garnished with coriander leaves.
