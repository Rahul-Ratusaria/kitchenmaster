<h1>Ingredients Of Tangra Macher Jhol</h1>

<br />mustard oil or cooking oil- 4 tablespoon
<br />fish pieces (tangra mach)- 8  
<br />tomato (medium size)- 1  
<br />green chili- 3  
<br />ginger paste- 1 tablespoon
<br />kalonji or kala zeera- 1 teaspoon
<br />cumin powder- 2 teaspoon
<br />salt- 1 tablespoon
<br />turmeric powder- 1/2 teaspoon
<br />coriander leaves (chopped)- 3 tablespoon
</div>

<b><h1>How to Make Tangra Macher Jhol</h1></b>
1). Marinate the fish with 1 tea spoon of salt and 1/2 teaspoon turmeric powder
<br />2). Make Ginger paste, cut the tomato and make other spices available.
<br />3). Heat oil in a pan
<br />4). Fry the fishes and keep them aside in a dish
<br />5). Add green chili, kalonji and tomato in the pan and fry a little
<br />6). Add cumin powder and turmeric powder with little water and cook for 2 minutes
<br />7). Add salt and green chili and water as required. Allow it to cook for 4-5 minutes.
<br />8). Add fishes and cook for 5 minutes in low flame. Add salt to taste and finally garnish with coriander leaves.


