<h1>Ingredients Of Butter Chicken</h1>
700 Gram Raw chicken
<br /><h2>For the Marinade:</h2>
1 tsp Red chilli powder
<br />1 tsp Ginger and garlic paste
<br />To taste Salt
<br />1/2 Kg Curd
<br /><h2>For the Gravy:</h2>
175 Gram White butter
<br />1/2 tsp Black cumin seeds
<br />1/2 Kg Tomato, pureed
<br />1/2 tsp Sugar
<br />1 tsp Red chilli powder
<br /><h1>To taste Salt</h1>
100 Gram Fresh cream
<br />4 Green chillies, sliced
<br />1/2 tsp Fenugreek leaves (crushed)
</div>

<b><h1>How to Make Butter Chicken</h1></b>
<b>For marination:</b>
<br /><br />1.In a mixing bowl, mix red chilli powder, ginger garlic paste, salt and curd.
<br />2.Add the raw chicken pieces to the marinade and mix well. Keep it in the refrigerator overnight or minimum 6 hours.
<br />3.Roast the marinated chicken in a tandoor or an oven for about 10-12 minutes until it is three-fourth done.
<br /><br /><b>Prepare the chicken gravy:</b>
<br /><br />1.Heat half the quantity of white butter in pan.
<br />2.Pour in the tomato puree, and saute for 2-3 minutes. Add cumin seeds, sugar, red chilli powder and salt. Mix well.
<br />3.Add the prepared chicken, white butter, fresh cream, sliced green chillies and crushed fenugreek leaves. Saute for 3-4 minutes and let the chicken cook.
<br />4.Cook till chicken is done. Serve hot with rice or naan.
<br /><br /><b>RECIPE NOTES</b>
<br /><br />This is a classic chicken dish and enjoyed best when served hot. You can also garnish it with cream and green chillies just before serving.
