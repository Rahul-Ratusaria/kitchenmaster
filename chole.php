<b><h1>Ingredients:</h1></b>
1 medium serrano pepper, cut into thirds
<br />4 large cloves garlic
<br />1 2-inch piece fresh ginger, peeled and coarsely chopped
<br />1 medium yellow onion, chopped (1-inch)
<br />6 tablespoons canola oil or grapeseed oil
<br />2 teaspoons ground coriander
<br />2 teaspoons ground cumin
<br />� teaspoon ground turmeric
<br />2� cups no-salt-added canned diced tomatoes with their juice (from a 28-ounce can)
<br />� teaspoon kosher salt
<br />2 15-ounce cans chickpeas, rinsed
<br />2 teaspoons garam masala
<br />Fresh cilantro for garnish
</div>

<b><h1>Preparation Of Chole</h1></b>
1).Pulse serrano, garlic and ginger in a food processor until minced. Scrape down the sides and pulse again. Add onion; pulse until finely chopped, but not watery.
<br />2).Heat oil in a large saucepan over medium-high heat. Add the onion mixture and cook, stirring occasionally, until softened, 3 to 5 minutes. Add coriander, cumin and turmeric and cook, stirring, for 2 minutes.
<br />3).Pulse tomatoes in the food processor until finely chopped. Add to the pan along with salt. Reduce heat to maintain a simmer and cook, stirring occasionally, for 4 minutes. Add chickpeas and garam masala, reduce heat to a gentle simmer, cover and cook, stirring occasionally, for 5 minutes more.
<br />4).Serve topped with cilantro, if desired.

