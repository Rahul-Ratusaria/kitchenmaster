<h2 style="padding-top:20px; margin:10px; color:#0000cc;">Cottage cheese bits in warm pureed spinach curry makes for this delicious dish called Palak Paneer. One of the most popular North Indian dish that goes well with just anything from roti, naan or parantha to even rice. It is basically soft paneer cubes cooked in a smooth spinach curry. This recipe is very easy to cook and can be made for dinner parties as well. Best served with some matar pulao and a chilled cucumber raita, a divine combination.</h2>

<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<b><h1>Ingredients Of Palak Paneer</h1></b>
500 Gram Spinach, pureed
<br />15-16 Paneer cubes
<br />2 Tbsp Oil
<br />1 tsp Cumin seeds
<br />1 Bay leaf
<br />1 tsp Ginger, finely chopped
<br />1 tsp Garlic, finely chopped
<br />1 Cup Onion paste (boiled), grated
<br />1/2 Cup Tomato, pureed
<br />2 tsp Salt
<br />1/2 tsp Garam masala
<br />1/2 tsp Red pepper, powdered
<br />1/2 tsp Coriander powder
<br />2 Nos Black cardamom (crushed)
<br />1 Tbsp Cream
</div>

<b><h1>How to Make Palak Paneer</h1></b>
1.Boil spinach in a pressure cooker and grind it to make palak puree.Palak Paneer.
<br />2.Heat oil in a pan and fry paneer cubes in it until golden brown.Palak Paneer.
<br />3.Remove the paneer cubes and put cumin, when it splutters, add bay leaf.Palak Paneer.
<br />4.When it starts to crackle add ginger, garlic and onion paste. Cook until it gets pinkish-brown in color.Palak Paneer.
<br />5.Add salt, garam masala, coriander powder along with red pepper. Stir till well mixed.Palak Paneer.
<br />6.Now add tomato puree and stir fry over medium heat.Palak Paneer.
<br />7.Add spinach and saute for 2-3 minutes.Palak Paneer.
<br />8.Now add paneer cubes to the mix and turn around a few times to completely mix it with the palak gravy.Palak Paneer.
<br />9.Pour some cream over, mix well and serve hot.

