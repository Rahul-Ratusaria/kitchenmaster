<h1>Ingredients Of Chicken Korma</h1>
1/2 Kg Chicken
<br />1 Cup Oil
<br />2-3 tsp Ghee
<br />8-10 Cardamoms
<br />6-7 Cloves
<br />2 Tbsp Garlic
<br />1 Tbsp Coriander powder
<br />1 Tbsp Chili
<br />To taste Salt
<br />1 tsp Ginger paste
<br />1 Cup Yogurt
<br />2 Onions (fried, puree this with the yogurt), sliced
<br />1 tsp Garam masala
<br />Few strands Saffron (mixed in 3 tsp of water)
</div>

<b><h1>How to Make Chicken Korma</h1></b>
1).Heat the vegetable oil into the pan, then put a dollop of ghee.
<br />2).Put cardamom, cloves, garlic and fry the mix properly.
<br />3).Then add the chicken and let it cook for about 2-3 minutes.
<br />4).Keep stirring it.
<br />5).Once it becomes brown, add coriander and chili powder.
<br />6).Add salt to taste.
<br />7).Add ginger paste, mixture of fried onions and yoghurt, let it cook for a minute.
<br />8).Then put garam masala and saffron for flavor.
<br />9).If the gravy is too thick add little bit of water before covering it. The masala should fuse well with the chicken.
<br />10).Cover it and give it some time to cook on slow fire. Let it simmer for about 10-15 minutes.
<br />11).Keep stirring occasionally.
<br />12).Serve hot.

