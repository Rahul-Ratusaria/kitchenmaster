<h2 style="padding-top:20px; margin:10px; color:#0000cc;">Delicious dal cooked in spices and generous amounts of ghee, butter, cream and curd. Direct from Punjab, this maa ki dal recipe won't disappoint you!</h2>

<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<b><h1>Ingredients</h1></b>
15 papdi
<br />1/2 teaspoon powdered cumin powder
<br />1/4 teaspoon powdered red chilli
<br />1 teaspoon salt
<br />1 tablespoon crushed to paste mint leaves
<br />1/2 teaspoon chilli garlic paste
<br />2 tablespoon sweet tamarind sauce
<br />1/4 teaspoon powdered chaat masala powder
<br />1 teaspoon sugar
<br />1 tablespoon coriander chutney
<br />2 teaspoon chopped coriander leaves
<b><br />For Garnishing</b>
<br />3 tablespoon sev
<b><br />For Toppings</b>
<br />1 cup beaten yoghurt (curd)
<br />1/4 cup bean mung
<br />2 peeled,parboiled,chopped potato
</div>

<h1><b>How to make Dahi Papdi Chaat</b></h1>
<br />1).Take a bowl and add yoghurt to it. Whisk the yoghurt until smooth. Add a pinch of salt and sugar to the yoghurt and mix well so that the flavour gets incorporated.
<br />2).Now take a plate and arrange the papdis on it. Peel the par boiled potatoes and chop them into cubes. Once the papdis are arranged, add some chopped potatoes on top. Now, top up with mung beans. Drizzle the coriander mint chutney and tamarind dates chutney on top ( you can add more as per your taste).
<br />3).Sprinkle chaat masala powder, cumin powder, red chilli powder and little salt. Drizzle the yoghurt on top followed by any remaining chutneys. Garnish with coriander leaves and sev. Serve at once. This delicious chaat recipe is best enjoyed on festivals like Holi and Diwali.

