<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Lassi</h1>
750 gm yoghurt
<br />50 gm ice cubes
<br />Salt or sugar
<br />200 ml water
</div>

<b><h1>How to Make Lassi</h1></b>
1).Crush the ice cubes in a blender.
<br />2).Add the yoghurt, water, salt or sugar.
<br />3).Blend for about 1 minute.
<br />4).Serve chilled.
<br />5).If you are making a namkeen lassi you can add masala like bhuna jeera and kala namak and as desired.
