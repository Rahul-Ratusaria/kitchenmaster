<h1>Ingredients Of Nadroo Yakhni</h1>
700-800 Gram Chicken pieces
<br />6-7 Tbsp Oil
<br />1 Cup Curd
<br />2 Pieces Green cardamom
<br />2 Pieces Brown cardamom
<br />2 Small Cinnamon stick
<br />1 tsp Saunth
<br />1 tsp Saunf powder
<br />A pinch of Kashmiri red chilli powder
<br />To taste Salt
</div>

<b><h1>How to Make Nadroo Yakhni</h1></b>
1).Heat the oil and fry the chicken till brown.
<br />2).Once done, remove the chicken and keep aside.
<br />3).In the same pan saute the cardamom and cinnamon. Mix a bit of water into the saunf and saunth powders and add them to the pan. Cook for 2-3 minutes, switch off the fire and bring the pan to room temperature.
<br />4).Now mix the curd into the pan and cook on a slow fire for two minutes. Then add the chicken to the curd. Sprinkle the chilli powder and salt. Stir, cover and cook for 10-15 minutes till done.
<br />5).Serve Chicken Yakhni hot with rice.


