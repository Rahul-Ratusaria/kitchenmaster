<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Daal Bati Churma</h1>
2 cups atta
<br />1 Tbsp soojee (rawa)
<br />3 Tbsp ghee (45 gm)
<br />1 tsp or to taste salt
<br />1/2 cup milk
<br /><b>For the Filling:</b>
<br />2 cups shelled peas
<br />1 Tbsp ghee
<br />1 tsp jeera
<br />1/8 tsp heeng powder
<br />1 Tbsp ginger, chopped fine
<br />2 tsp dhania powder
<br />1/4 tsp garam masala
<br />1/2 tsp haldi
<br />1/2 tsp or to taste chilli powder
<br />1 tsp or to taste salt
<br />1/2 tsp amchoor powder
<br />Ghee, kept hot to dip the baatis in.
</div>

<b><h1>How to Make Daal Bati Churma</h1></b>
1).Make stiff dough with the atta, soojee, 3 tbsp ghee, salt and milk. Keep covered for 15 minutes.
<br />2).Make the filling: heat the ghee and add jeera and heeng. Stir a few times and add the ginger.
<br />3).When slightly brown, add the peas, dhania, garam masala, chilli powder, salt and amchoor.
<br />4).Cook till tender and cool.
<br />5).Shape dough into flat rounds, fill with peas' filling, cover and form into round balls.
<br />6).Bake in a pre-heated oven for 45-50 minutes 10 minutes at 375 F, then lower the temperature to 350 F, till brown and crusty (if not brown on top, turn upside down and bake another 10 minutes.
<br />7).Remove from oven and dip into ghee, and transfer onto a serving dish.

