<h1>Ingredients Of Undhiyu</h1>
100 gms yam- peeled and diced
<br />100 gms potato- peeled and diced
<br />100 gms beans- diced
<br />100 gms raw banana- diced
<br />1/2 tsp asafoetida
<br />1/2 tsp whole cumin
<br />Oil to fry
<br /><b>For the muthiya:</b>
<br />2 cups chickpea flour
<br />1 tsp kastoori methi
<br />1 tsp red chilli
<br />1 tsp sathri or lemon flower
<br />1 tsp sugar
<br />2 tsp oil for binding
<br />Salt to taste
<br />1/4 tsp cumin
<br />1/2 tsp turmeric
<br /><b>For the masala:</b>
<br />1 Tbsp grated coconut
<br />3 tsp coriander powder
<br />1/2 tsp cumin
<br />1 Tbsp fresh coriander- chopped
<br />1/2 tsp turmeric powder
<br />1 tsp red chilli powder
<br />1/2 tsp oil
<br />Salt to taste

</div>

<b><h1>How to Make Undhiyu</h1></b>
1.Lightly fry all the vegetables and keep aside.
<br />2).To prepare muthiya, mix all the ingredients in a bowl.
<br />3).Make small balls from this mixture and fry.
<br />4).To prepare the masala, mix all the ingredients. Keep aside.
<br />5).Heat oil in a pan and add asafoetida and cumin.
<br />6).Now add the masala mixture and cook for few minutes.
<br />7).Finally add all the vegetables and muthiya balls
<br />8).Cook till done.



