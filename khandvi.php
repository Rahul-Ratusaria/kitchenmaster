<h1>Ingredients Of Khandvi</h1>
1/2 cup (60 gm) besan (Chick-pea flour)
<br />1/2 cup (60 gm) khatta dahi (sour yogurt)
<br />1 1/2 cups (375 ml) cup water
<br />1/2 tsp ginger paste
<br />1/2 tsp green chilli paste
<br />1/2 tsp red chilli powder
<br />1 tsp salt or to taste
<br />1/8 tsp heeng (asofoetida)
<br />1/8 tsp haldi (turmeric)
<br /><b>For tempering:</b>
<br />2 tsp oil
<br />1/2 tsp sarson (mustard seeds)
<br />2 sabut lal mirch (dried whole red pepper)
<br />4-5 kadhi patta (curry leaves)
<br />1 Tbsp hara dhania (coriander leaves) chopped fine
<br />1/4 cup grated coconut

</div>

<b><h1>How to Make Khandvi</h1></b>
<br />1).Place flour in a deep, heavy based pan (the pan should be large, as there is a lot of spurting while cooking).
<br />2).Add ginger-chilli paste, chilli powder, salt, heeng and haldi, and mix. Now add the yogurt, a little at a time, so as to form a smooth paste, without any lumps, and then the water.
<br />3).Place the pan over high heat, and bring to a boil, stirring all the time (to avoid scorching).
<br />4).Keep cooking and stirring till you reach a paste like consistency, increasing or decreasing the heat according to your ability to avoid scorching.
<br />5).It is cooked enough, when you separate a portion of it from the rest with a stirrer and you can see the bottom of the pan, which gets covered gradually (if it gets covered immediately, it is too thin). Another test is to spread a tsp of the batter on to an ungreased surface, when cool, it should come off clean.
<br />6).With the help of a rubber spatula spread the mixture onto an ungreased surface, in as thin a layer as possible and leave to cool.
<br />7).Heat the oil in a small pan and add the sarson, kadhi patta and sabut lal mirch. Stir a few times and then spread over the layer. Sprinkle all but 1 tbsp of the coriander and 1tbsp of the coconut over it, and pick up the lal mirch and keep aside.
<br />8).Cut this layer into strips roll up each strip like a scroll, as firmly as you can, without breaking them.
<br />9).Arrange them on to a serving dish. Garnish with the rest of the coriander, coconut and peppers and serve.



