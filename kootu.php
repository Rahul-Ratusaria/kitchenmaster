<h1>Ingredients Of Kootu</h1>
<br />3 tbsp Golden Harvest Tuvar Dal
<br />2 tbsp Peanuts
<br />Salt to taste
<br /><b>For Vegetables</b>
<br />1/2 cup carrot Chopped
<br />1/2 cup Beans Chopped
<br />1/2 cup Potato Peeled and cubed
<br />1/2 cup Pumpkin Cubed
<br />1/2 cup Madras Cucumber Cubed
<br />1/4 cup Peas
<br />1/2 cup Water
<br />Salt to taste
<br />1/2 tsp Turmeric Powder
<br /><b>For grinding into a paste</b>
<br />2 tsp Vegetable Oil
<br />1 tbsp Coriander Seeds
<br />1/4 tsp Fenugreek Seeds
<br />1/2 tsp Cumin Seeds
<br />3-4 Dry Red Chillies
<br />2 tsp Chana Dal
<br />1/4 cup Fresh Coconut Grated
<br /><b>For tempering</b>
<br />2 tsp Vegetable Oil
<br />1 tsp Mustard Seeds
<br />1 tsp Urad Dal
<br />10-12 Curry Leaves
</div>

<b><h1>How to Make Kootu</h1></b>
1).Wash tuvar dal and add it in a pressure cooker along with peanuts, salt and 1 cup of water.
<br />2).Pressure cook until dal is nicely cooked.
<br />3).Remove the pressure cooker from heat and let the pressure release.
<br />4).Mash the dal and keep it aside.
<br />5).Add all the vegetables in a pressure cooker along with salt, turmeric powder and 1 cup of water and pressure cook until they are softened.
<br />6).Remove the pressure cooker from heat and let the pressure release.
<br />7).Keep the cooked vegetables aside.
<br />8).Heat vegetable oil in a pan.
<br />9).Add coriander seeds, fenugreek seeds, cumin seeds, dry red chillies and chana dal and fry until they turn slightly brown.
<br />10).Remove the pan from heat and let the mixture cool.
<br />11).Add the mixture in a grinder along with coconut and some water and grind to make smooth paste.
<br />12).Now in a pan, add cooked vegetables, cooked dal and masala paste and cook for 10-12 minutes on low heat.
<br />13).Add more water if required.
<br />14).If you plan to serve the Kootu with rice, you can keep it thinner and if you want to serve as a side dish for chapati, you can keep it thicker.
<br />15).For tempering, heat vegetable oil in a pan.
<br />16).Once the oil is hot, add mustard seedss, urad dal and curry leaves and let them crackle for a few seconds.
<br />17).Pour the tempering over the kootu and mix well.
<br />18).Serve hot with rice or chapati.

