<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<b><h1>Ingredients Of Malai Kofta</h1></b>
<br />4 Large Potatoes, boiled
<br />250 Gram Paneer (cottage cheese)
<br />50 Gram Maida
<br />1 Tbsp Coriander leaves, chopped
<br />3 Onions
<br />1 Tbsp Ginger garlic paste
<br />2 Tomatoes
<br />200 Ml Malai or cream
<br />2 Tbsp Raisins and cashew nuts
<br />50 Gram Cashew nuts paste
<br />1/2 tsp Haldi (turmeric)
<br />1/2 tsp Red chilli powder
<br />1/2 tsp Kitchen king masala
<br />1 Tbsp Kasturi methi (dry Fenugreek)
<br />To taste Salt
<br />1 Tbsp Sugar
</div>

<b><h1>How to Make Malai Kofta</h1></b>
<b>Prepare the koftas:</b>
<br />1.Refrigerate the boiled potatoes for 4 to 6 hours as this makes it easy to cook koftas.
<br />2.Mash the boiled potatoes, paneer, maida. The mix should not be too hard or too soft. Add salt, chopped coriander leaves and mix well.
<br />3.Cut the raisins and cashew nuts into very small pieces and add 1/2 tsp of sugar to the mix.
<br />4.Heat up the oil for deep frying.
<br />5.Roll out the balls from the dough you prepared and stuff the dry fruit mix in the centre.
<br />6.Fry the koftas and if they break in hot oil then dust them with dry maida before putting them in.
<b><h1>Prepare the gravy:</h1></b>
<br />1.Fry some onion, ginger garlic paste and tomato paste.
<br />2.Mix the cashew nut paste with 2 Tbsp of warm milk and pour it into the paste.
<br />3.Except kasturi methi, add all the dry masala into the paste and saute till the oil separates itself.
<br />4.Add and a half cup of water and simmer the gravy till it's done.
<br />5.Add cream/malai, 1 Tbsp of sugar and kasturi methi.
<br />6.Simmer the gravy till the oil starts separating and once it's done, put the fried koftas into the gravy and serve hot with chapatis.

