<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Yoghurt Lumb Curry</h1>
700 gm assorted lamb-cut into large pieces
<br />2 1/2 liters water
<br />1 1/4 tsp ground garlic
<br />Salt to taste
<br />8 green cardamoms
<br />4 black cardamoms
<br />6 cloves
<br />2 tsp turmeric powder
<br />1/2 tsp dry ginger powder
<br />1 1/2 Tbsp onion paste-fried
<br />1 cup yogurt
<br />2 tsp Kashmiri red chilli powder-dissolved in 1/2 cup water
<br />1/2 cup desi ghee
<br />1/2 cup dry cockscomb (mawal) flowers-heated with 1/2 cup water; extract
<br />1/2 tsp black cumin seeds
<br />1/2 tsp dry mint leaves

</div>

<b><h1>How to Make Yoghurt Lumb Curry</h1></b>
1).Bring the water to a boil in a deep pan.
<br />2).Add the lamb and remove the scum that rises to the surface.
<br />3).When the water is clear, add the garlic and continue to boil for 4-5 minutes.
<br />4).Add the salt and cook until the lamb is half done.
<br />5).Add the green and black cardamoms, cloves, turmeric powder, ginger powder, onion paste, yogurt, red chilli water and ghee.
<br />6).Mix well, reduce heat and cook covered until the lamb is tender.
<br />7).Add the cockscomb flower extract and black cumin seeds.
<br />8).Mix well and sprinkle dry mint leaves.


