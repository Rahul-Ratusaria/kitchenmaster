<h1>Ingredients Of Methi Ka Thepla</h1>
2 Cups Atta
<br />2 Tbsp Oil
<br />1 Tbsp Dried Methi
<br />2 tsp Salt
<br />2 tsp Ginger and Green Chillies (make paste)
<br />1 tsp Garlic
<br />2 tsp Coriander Powder
<br />1 tsp Sugar
<br />To knead Yogurt
<br />As needed Water

</div>

<b><h1>How to Make Methi Ka Thepla</h1></b>
1.Take all ingredients in a bowl. Mix well.
<br />2).Knead the ingredients well with yogurt and some water.
<br />3).Make thin paranthas out of the dough.
<br />4).Cook the paranthas from both sides, over high flame till greenish brown.
<br />5).Serve hot.


