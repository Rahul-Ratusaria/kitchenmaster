<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Gongura Mamsam</h1>
<br />250 gm lamb meat
<br />Turmeric powder to taste
<br />Salt to taste
<br />Ginger-garlic paste
<br /><b>For the curry:</b>
<br />2 Tbsp refined oil
<br />6 cloves
<br />3 cardamoms
<br />1 stick of cinnamon
<br />1/2 tsp cumin seeds
<br />3 Tbsp chopped onions
<br />1 tsp ginger garlic paste
<br />100 gm gongura leaves
<br />1 Tbsp red chilli powder
<br />1 tsp coriander powder
<br />1/2 tsp turmeric powder
<br />10 curry leaves
<br />6 slit green chillies
<br />Chopped coriander leaves
<br />50 ml water
<br />1 1/2 tsp salt
<br />Fried red chilli, curry leaves
<br />Clarified butter
</div>

<b><h1>How to Make Gongura Mamsam</h1></b>
1).Boil the meat with turmeric powder, salt and ginger garlic paste, keep aside.
<br />2).Now heat oil in pan.
<br />3).Add cloves, cardamoms, cumin seeds and chopped onions.
<br />4).Saute onions till they turn translucent.
<br />5).Then add ginger garlic paste, gongura leaves, red chilli powder, coriander powder, turmeric powder, curry leaves, slit green chillies, chopped coriander leaves, boiled meat, water and salt.
<br />6).Mix well and cook for 5 minutes.
<br />7).Garnish it with fried red chillies, curry leaves and clarified butter.
<br />8).Serve hot with rice.

