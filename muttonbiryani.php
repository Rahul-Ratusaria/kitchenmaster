<h3 style="padding-top:20px; margin:10px; color:#0000cc;">A hearty mutton biryani that will amaze your guests! Layers of mutton and saffron-milk infused rice cooked 'dum' style.</h2>
<div>

<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Mutton Biryani</h1>
<b>For the rice:</b>
<br />1 Star anise
<br />500 Gram Basmati rice, blanched
<br />2 Bay leaves
<br />2 Black cardamom
<br />2 tsp Black cumin seeds
<br />6 Black peppercorn
<br />6 Green cardamom
<br />2 Cinnamon sticks
<br />6 Cloves
<br />1 tsp Fennel
<br />1/4 Jaiphal
<br />1 Javitri
<br />3 tsp Salt
<br /><b>For mutton marination:</b>
<br />1 Kg Mutton (cut in 2 inches pieces, preferably front leg part and avoid shoulder cut)
<br />1 Tbsp Garam masala
<br />1 Tbsp Garlic paste
<br />1 Tbsp Ginger paste
<br />3 Tbsp Raw papaya paste
<br />4 Tbsp Hung curd
<br />1 Lemon (juiced)
<br />Coriander powder
<br />Cumin powder
<br />1 Tbsp Red chilli powder
<br />1 tsp Salt
<br /><b>Other ingredients:</b>
<br />4 Onions, sliced
<br />2 Tomatoes, chopped
<br />1/4 Cup Milk (warm)
<br />Ghee
<br />Saffron strands
<br />Oil
<br />Rose water
<br />Kewra essence
<br />4 green chillies
</div>

<b><h1>How to Make Mutton Biryani</h1></b>
<b>Marinating the mutton:</b>
<br />1).To the mutton add the beaten curd, ginger-garlic paste, raw papaya paste, chilli powder, salt, lemon juice, garam masala.
<br />2).Allow the mutton to marinate for 3 hours.
<br /><br /><b>Preparing fried onions or Barista:</b>
<br />1).Slice 2 onions very thinly. Separate the slices.
<br />2).In a pan or kadai add oil and fry the onion slices till nicely brown.
<br />3).Take care not to burn them. Fry in small batches. Do not put all the slices at a time, that will lead to lumpy messy onions.
<br />4).Make sure all the onion slices frying are dipped in oil, if needed add more oil. Keep stirring continuously but gently for an even brown color.
<br />5).Take the fried onions out with a slotted spoon or ladle.
<br />6).Keep them on a paper-towel lined plate. This crispy brown fried onion slices are called Barista.
<br /><br /><b>Cooking the mutton:</b>
<br />1).Heat ghee in a thick-bottomed pan.
<br />2).Add remaining sliced onions and green chillies. Cook, stirring continuously, till onions are light golden brown.
<br />3).Add ginger paste and garlic paste and mix well.
<br />4).Add marinated mutton and cook on high heat for seven to eight minutes.
<br />5).Add coriander powder, cumin powder and red chilli powder. Mix thoroughly.
<br />6).Stir in three cups of water, bring it to a boil, reduce heat and cook covered till mutton is almost cooked.
<br />7).Add tomatoes, salt, garam masala powder and fresh coriander leaves.
<br />8).Cook for 15 minutes on medium heat, stirring occasionally. The ghee would be separated from the spices and there should not be any watery gravy to the meat.
<br /><br /><b>Preparing the rice:</b>
<br />1).Use only good quality long grain Basmati rice. Soak the rice for 20 minutes in water. Wash well till the water runs clear. Drain all the water.
<br />2).In a small piece of cloth take cardamom, cinnamon, cloves, javitri, jaiphal, black peppercorn, shahi jeera, star anise and tie a knot to make a bag (potli).
<br />3).Bring 750 ml water to boil, add rice, bay leaf, salt and potli, cover and cook till rice is done 1/3rd.
<br />4).Drain the water & remove the whole masala potli.
<br /><br /><b>Preparing the saffron-milk:</b>
<br />1).Take 1/4th cup warm milk in a cup and dissolve saffron strands in the milk.
<br />2).Cover and wait for 20 minutes. Add rose water and kewra essence in the milk. Mix well and cover. Keep aside.
<br /><br /><b>Layering the biryani:</b>
<br />1).Take a large heavy bottom pan with tight fitting lid.
<br />2).Add 2tbsp ghee to the pan. Melt the ghee on low heat. Turn and rotate the pan carefully so the ghee can coat the bottom and sides of the pan. Switch off the heat.
<br />3).Add a layer of cooked rice, then cooked meat pieces, sprinkle saffron water, add fried onion slices and ghee.
<br />4).Again add a layer of rice, then meat...go on like this till you are done. Top and bottom layer will be of rice.
<br />5).Cover with chopped pudina and coriander, fried onion and slit green chillies and juice of half a lemon.
<br />6).Put the lid on. Seal the pan with flour dough or aluminium foil, then put the lid. Keep the heat to lowest. And cook the Biryani in this 'Dum' process for 40 minutes.
<br />7).Make sure your pan is heavy-bottomed or the rice will burn. Or you can place a flat tawa then keep the pan on that tawa.
<br />8).After 40 minutes switch off the heat and let the biryani stand for another 10 minutes. Transfer to a serving bowl.
<br />9).Serve with raita and salad.

