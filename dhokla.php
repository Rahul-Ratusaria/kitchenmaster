<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Dhokla</h1>
1 Cup Gram flour (besan)
<br />1 Tbsp Citric acid
<br />1 Tbsp Sugar
<br />To taste Salt
<br />A pinch of Turmeric
<br /><b>To make batter Water</b>
<br />1 Tbsp Fruit salt/ baking powder (dissolved in water)
<br /><b>For the Tadka:</b>
<br />1 Tbsp Oil
<br />1/2 Tbsp Mustard seeds
<br />1 Dried red chilli
<br />7-8 Curry leaves

</div>

<b><h1>How to Make Dhokla</h1></b>
1).In a bowl mix gram flour, citric acid, salt, sugar and turmeric. Add water and make it into a smooth batter with medium thick consistency.
<br />2).In a glass make add the fruit salt or baking powder. Add water to it and pour this into the dhokla mixture.
<br />3).Grease the steaming tin with 2 drops of oil and por the mixture into it. Steam for about 15-20 minutes or till cooked.
<br />4).In a pan, add oil, mustard seeds, curry leaves and red chillies. Let it splutter.
<br />5).Pour the tadka over the prepared dhokla.
<br />6).Cut into pieces and serve.


