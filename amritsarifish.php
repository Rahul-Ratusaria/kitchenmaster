<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Amritsari Fish</h1>
1 kg singhada fish
<br />Salt to taste
<br /><b>For the batter:</b>
<br />1 cup gram flour
<br />2 tsp garam masala
<br />2 tsp cumin powder
<br />1 tsp red chilli powder
<br />Salt to taste
<br />1 tsp turmeric powder
<br />Water
<br />Lemon juice to taste
<br />Oil, for deep frying
</div>

<b><h1>How to Make Amritsari Fish</h1></b>
<b><h2>For the batter:</h2></b>
1).In a bowl add gram flour, garam masala, cumin powder, red chilli powder, salt , turmeric powder and enough water to make the batter of coating consistency. Mix it well. The batter is ready.
<b><h2>For the main preparation:</h2></b>
1).Clean the fish and slice it into pieces. Then add salt and leave it for about 3 hours.
<br />2).Wash off the salt and coat the fish pieces with the prepared batter.
<br />3).Deep fry till it is done. Machchli Amritsari is ready to eat.
<br />4).Garnish with garam masala and lemon juice.
<br />5).Serve hot.
