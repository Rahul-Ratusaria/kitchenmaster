<h1>Ingredients Of Keema Muttor</h1>
1/2 Kg Meat (minced)
<br />1 Cup Green peas (shelled)
<br />1/2 Cup Ghee
<br />2 tsp Cumin seeds
<br />4 Cloves
<br />1 piece Small Cinnamon
<br />4 Peppercorns
<br />1 Black cardamom (seeds)
<br />2 Bay leaves
<br />1 Cup Onions, grated
<br />1 tsp Ginger paste
<br />1 tsp Garlic paste
<br />2 Cups Tomatoes, chopped
<br />To taste Salt
<br />1 Tbsp Coriander powder
<br />1/2 tsp Turmeric
<br />1/2 tsp Chilli powder
<br />1 Tbsp Coriander leaves, chopped
</div>

<b><h1>How to Make Keema Muttor</h1></b>
1).Heat the ghee in a heavy-based pan and add the cumin seeds, cloves, cinnamon, peppercorn, cardamom and bay leaves.
<br />2).When the seeds start to splutter, add the garlic, ginger and onions, and stir-fry till the fat separates.
<br />3).Add the tomatoes, salt, coriander powder, turmeric and chilli powder.
<br />4).Continue to stir-fry till fat separates. Increase heat to high and add the mince and the peas.
<br />5).Stir a few times till the mince looks fried, then lower heat and saut till cooked through and fat separates once again.
<br />6).Serve hot, garnished with chopped coriander leaves.


