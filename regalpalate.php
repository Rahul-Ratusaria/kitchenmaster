<h1>Ingredients Of Regal Palate</h1>
1kg Regal Marlborough Salmon, skin on bones out
<br />2 beetroots, finely shredded
<br />1 cup brown sugar
<br />1 cup Marlborough sea salt
<br />2 tbsp fresh parsley plus stalks
<br />1 tbsp fresh chervil
<br />1 tbsp fresh tarragon
<br />1 tbsp fresh coriander plus stalks
<br />2 tbsp coriander seeds toasted
<br />2 tbsp fennel seeds
<br />1/2 tsp cracked black pepper
<br />Minced zest of 1 lemon, lime & orange
<br />1 tsp of crushed mustard seeds
<br />Cheesecloth to wrap salmon




</div>

<b><h1>How to Make Regal Palate</h1></b>
1).Cut the salmon in half. Mix all ingredients together in a large bowl and smear over salmon. Place salmon pieces together with flesh touching each other (skin facing out). 
<br />2).Wrap salmon in cheesecloth, leaving no gaps. Place in the fridge, in an airtight container and leave for 36 hours.
<br />3).If you like you can blend the beetroot, sugar, salt and the toasted seeds together and leave any left-over mix in the fridge for later use.
<br /><b>To Serve:</b>
<br />Remove salmon from cheesecloth. Rinse in cold water and pat dry.  Carefully slice a piece of the salmon to taste - see how delicious it is! Slice as needed, to be served as sashimi.
<br />Garnish with sliced beetroots, radishes and any other ingredient used for the curing.

