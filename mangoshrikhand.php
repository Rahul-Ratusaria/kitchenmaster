<h1>Ingredients Of Mango Shrikhand</h1>
2 Cups Hung yogurt (hung curd)
<br />1/2 tsp Saffron
<br />2 Cups Condensed milk, large
<br />300 Gram Fresh mangoes, chopped
<br />1/2 Cup Sugar
<br />2 tsp Green cardamom powder
<br />1/4 Cup Fresh cream
<br />1 Lemon (juiced)
<br />20 Gram Mint
<br />2 tsp Chaat masala
<br />4 Pieces Gold varq
<br />80 Gram Pistachio, unsalted

</div>

<b><h1>How to Make Sandesh</h1></b>
1).Mix hung curd with saffron, condensed milk, chopped mangoes, sugar, cardamom powder and cream. Leave to chill.
<br />2).Prepare mango salad by mixing diced mango with lemon juice, mint and chaat masala.
<br />3).Serve shrikhand with mango salad. Garnish with gold varq and pistachios.

