<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Nargisi Kofta</h1>
1/2 kg mince meat
<br />2 onions quartered
<br />2 onions chopped
<br />2 tomatoes pureed
<br />4-5 green chillies
<br />5-6 whole garlic cloves
<br />1 Tbsp chopped ginger
<br />3 tsp red chilli powder
<br />1/2 tsp turmeric powder
<br />1 tsp garam masala
<br />1 tsp coriander powder
<br />2 Tbsp yoghurt
<br />1 egg beaten
<br />4 eggs-hard boiled
<br />Oil for frying
<br />Salt to taste

</div>

<b><h1>How to Make Nargisi Kofta</h1></b>
1).Cook the minced meat for about 20 minutes with the quartered onions, green chillies, chopped ginger and garlic cloves.
<br />2).Once cooked beat 1 egg into the mixture.
<br />3).Coat the boiled eggs with the mince.
<br />4).Fry the coated eggs in hot oil and keep aside.
<br />5).To the remaining oil in the pan, add the chopped onions and the ginger.
<br />6).Fry until the onions turned brown.
<br />7).Add 1 tsp red chilli powder, coriander powder, yoghurt, pureed tomatoes and the garam masala to the pan.
<br />8).Simmer until the gravy turns thick.
<br />9).Slice the fried eggs in half and serve with the gravy.

