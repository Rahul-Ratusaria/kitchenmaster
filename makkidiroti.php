<h1>Ingredients Of Makke ki Roti</h1>
2 cups Makki ka atta (Cornmeal)
<br />1/2 cup Wheat Flour (Atta)
<br />1-1/4 cup Water (a little less less that 1-1/4 cup)
<br />Salt to taste
<br />3/4 teaspoon Ajwain/ Carrom seeds
<br />1/4 cup Oil or Ghee
</div>

<b><h1>How to Make Makki Ki Roti</h1></b>
Assemble all the ingredients.
<br />Add all the ingredients in a big bowl and knead a soft dough.
<br />Add water slowly and gradually, do not pour at once.
<br />Dough is all smooth and ready.
<br />Take a plastic ziploc bag and cut it's three sides. After cutting open the plastic bag, it will open like a book. Grease the plastic sheet in the center.
<br />Make a small ball out of the dough and place it in between the sheets of plastic. Drizzle some oil on top of it.
<br />Fold the plastic sheet over the dough ball.
<br />Heat and grease a griddle on medium heat. Using your hands, spread the dough ball around making it look like a large disc.
<br />Roti all ready.
<br />Remove the plastic sheet from the top of the roti ...
<br />Carefully lift the roti and...
<br />Transfer it on to hot griddle/ tawa.
<br />With a knife, make a cut in the center.
<br />Grease along the way. When cooked from one side, flip it over and..
<br />Cook on each side, until brown and cooked through. Apply oil/ghee as needed.
<br />Crispy brown makki ki roti is ready.
<br />Repeat the process with rest of the dough.
<br />Serve it hot with gajar, shalgum ka achar and
<br />Warm sarson ka saag!!

