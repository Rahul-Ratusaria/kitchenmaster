<h1>Ingredients Of Chicken Pulao</h1>
500 Gram Rice
<br />6-7 Chicken thighs
<br />2 tsp Cumin seeds
<br />2 tsp Coriander powder
<br />3-4 pods Green cardamom
<br />3-4 Sticks Cinnamon
<br />2 tsp Mace
<br />Few Black pepper corns
<br />5-6 cloves Garlic, chopped
<br />5-6 Raisins
<br />1 Cup Yogurt
<br />2 Tbsp Desi ghee
<br />1 Cup Onion, sliced
<br />2 tsp Coriander seed powder
<br />1 tsp Red chilli powder
<br />1 Ginger, sliced
<br />Cooking oil
<br />Salt & water

</div>

<b><h1>How to Make Chicken Pulao</h1></b>
1).Take rice, wash it properly and soak it in some water for 20 minutes.
<br />2).Take 6-7 chicken thighs and cut it into even size pieces.
<br />3).Now wash these pieces well and drain the excessive water out, nicely.
<br />4).Take 1 cup of yogurt and add salt, 1 tsp red chilli powder, 1 tsp turmeric powder, 3 tsp coriander seed powder and 1 tsp black pepper powder.
<br />5).Whisk the yogurt nicely and keep it aside.
<br />6).Heat 2 tbsp desi ghee in a pan, add 4-5 cloves of chopped garlic, add 5-6 cloves,1 cup sliced onions and saute.
<br />7).Now add the finely chopped ginger and 5-6 chopped green chillies and saute.
<br />8).Now add the whisked yogurt mixture and saute.
<br />9).Cook for 2 minutes and once you see the specs of fat coming up, add the chicken pieces.
<br />10).Add some salt too at this stage.
<br />11).Cook for 1 minute and add 2 cups of water and leave it to boil. Use enough of water because the rice will be cooked in this.
<br />12).Let it simmer for a couple of minutes.
<br />13).When the chicken is cooked, add the rice to the chicken and allow it to cook.
<br />14).Gently stir the rice because basmati after soaking becomes very fragile.
<br />15).Once the rice is cooked, serve hot.


