<h1>Ingredients Of Sandesh</h1>
Milk 8 cups
<br />Castor Sugar 1/2 cup
<br />Lemon juice 1/4 cup
<br />Green cardamom powder as required
<br />Pistachios blanched and chopped 12

</div>

<b><h1>How to Make Sandesh</h1></b>
1).Bring the milk to a boil in a deep, thick-bottomed non-stick pan. Add the lemon juice and stir till the milk curdles. Strain and immediately refresh the chhenna in chilled water.
<br />2).Put the chhenna in a piece of muslin and squeeze till all the water is drained out.
<br />3).Knead the chhenna well with the heel of your hand. Add caster sugar and cardamom powder, and knead again.
<br />4).Cook in a non-stick pan on medium heat for eight minutes. Remove from heat and divide into twelve equal portions. Roll each portion into a ball and make a dent on the top.
<br />5).When cooled, place a pistachio in the dent and serve. Makes 12 sandesh/350 grams

