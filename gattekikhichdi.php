<h1>Ingredients Of Gatte Ki Khichdi</h1>
<b>For Dough of Gatte:</b>
<br />1 Cup Besan
<br />1 tsp Cumin Seeds , roasted
<br />1/2 tsp Turmeric Powder
<br />1/4 tsp Baking Soda
<br />1 tsp Red Chilli Powder
<br />1/2 tsp Salt
<br />1 tsp Coriander Powder
<br />2 tsp Mint Leaves
<br />1 tsp Ginger , chopped
<br />2 Tbsp Curd
<br />1 Cup Water
<br /><b>For Making Gatte:</b>
<br />2 Cups Water
<br />Oil
<br /><b>For Gravy:</b>
<br />1 Tbsp Oil
<br />1 Tbsp Ghee
<br />2 Nos Red Chilli Whole
<br />6-7 Nos Cloves
<br />1 Cinnamon Stick
<br />2 Onions, chopped
<br />1/2 tsp Salt
<br />1/2 tsp Turmeric Powder
<br />1/2 tsp Garlic
<br />1/2 tsp Red Chilli Powder
<br />1/2 tsp Coriander Powder
<br />1/2 tsp Hing Water
<br />2 tsp Coriander Leaves
<br />1 Cup Curd
<br />2 Cups Water
</div>

<b><h1>How to Make Gatte Ki Khichdi</h1></b>
1).Mix besan, cumin seeds, turmeric powder, baking soda, red chilli powder, salt, and coriander powder in a large deep bowl.Spicy Gatte Ki Sabzi
<br />2).Add mint leaves, chopped ginger and curd to it. Mix thoroughly.Spicy Gatte Ki Sabzi
<br />3).Now gradually add required quantity of water and knead a soft dough.Spicy Gatte Ki Sabzi
<br />4).Divide the dough into equal portions and roll each portion into long cylindrical shapes.Spicy Gatte Ki Sabzi
<br /><b>For Preparing Gatte:</b>
<br />1).Boil water in a pan. When it comes to boil, add the cylindrical gattas in the boiling water.Spicy Gatte Ki Sabzi
<br />2).Boil until they are fully cooked. Check by inserting a knife in the cylindrical rolls whether they have cooked properly.
<br />3).Let them cool down for a few minutes and then cut into long pieces.
<br />4).Now heat oil in a non- stick pan and shallow fry the gattas on medium flame till golden brown.Spicy Gatte Ki Sabzi
<br /><b>For Preparing Gravy:</b>
<br />1).In a heavy bottomed pan heat oil and ghee. Add red chilli whole, cloves and cinnamon stick. Saute for 20-30 seconds.Spicy Gatte Ki Sabzi
<br />2).Now add the chopped onions and cook until soft and translucent.Spicy Gatte Ki Sabzi
<br />3).Add salt and turmeric powder. Saute well.Spicy Gatte Ki Sabzi
<br />4).Now put garlic, red chilli powder, coriander powder, hing water and coriander leaves. Combine all the ingredients thoroughly. You can add more or less red chilli powder as per your taste.Spicy Gatte Ki Sabzi
<br />5).Add curd and mix well. Cook until oil appears on the surface.Spicy Gatte Ki Sabzi
<br />6).Put water in the mixture and bring it to boil over medium flame. When it becomes a semi-thick gravy add the fried gatte and cook further.Spicy Gatte Ki Sabzi
<br />7).Stir with light hands and make sure that the gattas are well coated with gravy.Spicy Gatte Ki Sabzi
<br />8).Garnish with green chillis and fresh coriander leaves.
<br />9).Serve this Spicy Gatte ki sabzi with chapatis or rice.Spicy Gatte Ki Sabzi

