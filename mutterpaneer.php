</h2>
<div>

<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Muttor Paneer</h1>
2 cups cubed (500 gm) paneer - deep fried to a light brown
<br />2 cups shelled green peas
<br />3-4 green chillies-slit a little
<br /><b>For the Gravy:</b>
<br />2 cups chopped onions
<br />1 tsp peeled cloves of garlic-ground with onions
<br />1 tsp chopped ginger
<br />1/2 cup tomato-grated or 1/4 cup puree
<br />1/4 cup oil
<br />2 tsp cumin seeds
<br />2 bay leaves (tej patta)
<br />1/2 tsp turmeric (haldi)
<br />1 Tbsp salt
<br />1/2 tsp garam masala
<br />1/2 tsp powdered red pepper
<br />1 Tbsp powdered coriander (dhania) seeds
<br />1 Tbsp chopped coriander leaves, for garnish
</div>

<b><h1>How to Make Mutter Paneer</h1></b>
1).Blend the onions, garlic and ginger to a paste.
<br />2).Heat oil and add the cumin seeds and the bay leaves.
<br />3).Add onion paste and saute till brown and fat separates.
<br />4).Add tomatoes, turmeric, salt, garam masala, red pepper and the coriander powder, and stir-fry till fat separates.
<br />5).Add the peas, paneer and the green chillies and saute over high heat till glossy.
<br />6).Add two cups water, bring to a boil and simmer for 5-10 minutes.
<br />7).Serve hot, garnished with the coriander leaves. Omit the chillies if you so desire.

