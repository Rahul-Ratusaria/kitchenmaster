<h1>Ingredients Of Bardoli Ki Khichdi</h1>
2 Cups Rice
<br />1 1/2 Cups Toor dal
<br />250 Gram Peas
<br />3-4 Potatoes (cubed)
<br />2-3 Green chilies
<br />1 Raw mango
<br />Jeera seeds
<br />Mustard seeds
<br />1 Tbsp Cumin seeds
<br />1 tsp Red chili powder
<br />1 tsp Turmeric powder
<br />Asafoetida
<br />2 Onions, chopped
<br />1/2 Ginger, chopped
<br />Ghee
<br />Sugar
<br />Salt
<br />For garnishing Fresh coriander

</div>

<b><h1>How to Make Bardoli Ki Khichdi</h1></b>
1).Heat 1 tsp of ghee in a large pot.
<br />2).Into this add cumin seeds and fry for two minutes.
<br />3).Add chopped onions and saute till brown.
<br />4).Add chopped ginger, split green chillies and fry on a low flame for 5 minutes.
<br />5).Add turmeric powder and red chilli powder and let it cook till the oil ghee separates from the masala.
<br />6).Add cubed potatoes, washed rice and washed dal and saute for 5 minutes.
<br />7).Add in a pinch of asafetida powder, green peas, a tsp of sugar and salt to taste.
<br />8).Also add raw mango, chopped into fine pieces.
<br />9).Mix together and add in 4 cups of water. Bring to a simmer, cover the pot and let it simmer for 20-25 minutes.
<br />10).Garnish the khichdi with fresh coriander and 2 tbsps of ghee.
<br />1)1.Serve hot.


