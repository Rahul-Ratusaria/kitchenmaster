<h3 style="padding-top:20px; margin:10px; color:#0000cc;">Here's for you a brinjal recipe straight from Kashmir. Brinjals cooked in mustard oil with kashmiri red chillies and varied spices.
</h2>
<div>

<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Khatte Baingan</h1>
3 Tbsp mustard oil
<br />1 cinnamon stick
<br />1 Tbsp cumin seeds (zeera)
<br />5 green cardamom (elaichi)
<br />1 Tbsp kashmiri red chillies
<br />1/2 tsp asafoetida (hing)
<br />1/2 tsp turmeric powder (haldi)
<br />1/2 tsp coriander powder (sookha dhaniya powder)
<br />Salt, to taste
<br />1 Tbsp dry ginger powder (saunth powder)
<br />2 sliced onion
<br />5 long brinjals
<br />1 sliced tomato
<br />1 tsp fennel seeds (saunf)
<br />Coriander leaves, to garnish
<br />1/2 lime

</div>

<b><h1>How to Make Khatte Baingan</h1></b>
1).In a wok (kadai) add mustard oil, cinnamon, cumin seeds, green cardamoms, kashmiri red chilies, asafoetida powder, turmeric powder, coriander powder and little salt. Mix them well together and add little water.
<br />2).Add dry ginger powder and combine them together.
<br />3).Add sliced onion and brinjals. Fry them well together.
<br />4).Cover them and let the brinjals cook for 20 mintues.
<br />5).Add the sliced tomatoes, fennel seeds and little water.
<br />6).Let the tomatoes cook.
<br />7).Sprinkle some coriander leaves and lime.
<br />8).Serve the Kashmiri khatte baingan hot.

