<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Kotte Kadubu</h1>
urad dal  1 cup
<br />rice rava or idli rava  2 cups
<br />salt as per taste
<br />Jackfruit leaves  4 leaves for 1 kadubu
<br />Stick from coconut leaves  few
</div>

<b><h1>How to Make Kotte Kadubu</h1></b>
1).Very first step is to make idli cups using jackfruit leaves.for making 1 cup we need 4 tender and fresh jackfruit leaves.
<br />2).To start with,first take two fresh jackfruit leaves and hold it as shown in the pictures below joining the two ends of leaves.With the help of small sharpened coconut stick join the two leaves and secure it tight.
<br />3).Now place the 3rd leaf on top and join them again with one more stick.Repeat the procedure with 4th leaf thus each leaf facing 4 directions.Now bend 2 leaves at once in upward direction while not leaving any gap in the corner.Join them again with coconut sticks.Repeat the procedure to completely seal the leaves and make it look like a cup as shown in the picture below.Make sure there are no gaps left in the corners so that batter doesnï¿½t leak or flow out through the gaps.
<br />4).Cut the excess parts of the leaves with a scissor.Like this keep all the kotte ready.
<br />5).To prepare the batter for kadubu,soak 1 cup of washed urad dal for 2 hours.After 2 hours grind the urad dal into a fine and thick batter.
<br />6).Meanwhile soak 2 cups of rice rava or idli rava in water for 10 minutes. After 10 minutes squeeze the water content and add squeezed idli rava into urad dal batter.
<br />7).If required add little water to the batter.Mix really well and bring the batter into right consistency.Batter should not be very watery,make it slightly thick but with pouring consistency.
<br />8).Allow the batter to ferment for 8 hours or overnight.
<br />9).Add required amount of salt to the fermented batter and mix well.
<br />10).Take 1 or 2 ladle full of batter and fill it inside the prepared kotte or the idli cups.
<br />11).Place the idli cups inside a idli vessel or the steamer and steam for 15 mins on high flame.
<br />12).Turn off the flame after 15 minutes.Allow the kadubu to cool for 5 minutes and unmould it.
<br />13).Traditionally kotte kadubu is served along with sweetened coconut milk and chutney combination.But it also tastes good with sagu or vegetable kurma.Non vegetarians can enjoy it with Chicken sukka or curry made with coconut.

