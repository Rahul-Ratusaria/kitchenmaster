<h1>Ingredients Of Llish Macher Jhol</h1>
Hilsa fish, cut into thin pieces.
<br />1 tsp black cumin seeds(kalongi) 
<br />1/2 tsp turmeric powder / haldi
<br />2 to 4 green chillies
<br />1 Tbsp mustard oil (or any vegetable oil)
<br />Salt to taste
</div>

<b><h1>How to Make Llish Macher Jhol</h1></b>
1).Shallow fry the Hilsa fish pieces, till golden brown. Keep aside.
<br />2).In a pan, heat 1 Tbsp oil, add about 1 tsp black cumin seeds and let them crackle.
<br />3).Add about a cup of water to it, about 1/2 tsp haldi, salt to taste and let the water come to a boil.
<br />4).Add the fried Hilsa pieces. Split the the green chillies into two or cut them into half and add to the curry.
<br />5).Cook for 2 mins.
<br />6).Hilsa curry is ready. Serve with steamed rice.



