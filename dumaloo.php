<h1>Ingredients Of Dum Aloo</h1>
750 gm small sized potatoes
<br />Oil for frying
<br />50 gm red chilli powder
<br />5 gm asafoetida
<br />30 gm cardamom powder
<br />150 gm yoghurt
<br />50 gm fennel powder
<br />25 gm dry ginger
<br />Salt to taste
<br />50 ml ghee
</div>

<b><h1>How to Make Dum Aloo</h1></b>
1).Boil the potatoes and pierce them with a toothpick or a fork.
<br />2).Heat oil and fry the potatoes on a medium flame until they are slightly brown, drain and keep aside.
<br />3).Heat ghee, add asafoetida, add the other spices and stir thoroughly.
<br />4).Whip the yoghurt. Add the yogurt and mix.
<br />5).Add rest of the ingredients and saute for 2 minutes. Sprinkle a little water while sauteing the ingredients.
<br />6).Add some more water to make the gravy and let it cook for about 10 minutes till gravy thickens.


