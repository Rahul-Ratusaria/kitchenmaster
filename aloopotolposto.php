<h3 style="padding-top:20px; margin:10px; color:#0000cc;">Potatoes fried in a thick coating of poppy seeds is a signature dish from Bengal.
</h2>
<div>

<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Aloo Potol Posto</h1>
3-4 potatoes - chopped
<br />3 Tbsp poppy seeds
<br />3 green chillies
<br />1 tsp cumin seeds
<br />1 tsp turmeric powder
<br />3 dried red chillies
<br />Salt to taste
<br />Fresh coriander for garnishing
<br />Oil
</div>

<b><h1>How to Make Aloo Potol Posto</h1></b>
1).Roast the poppy seeds for about a minute. Grind the roasted poppy seeds and green chillies into a paste with some water.
<br />2).Deep-fry the potatoes till they turn golden brown. Keep aside.
<br />3).In another pan heat 1 1/2 table spoon of oil. Add the cumin seeds, turmeric powder and dried red chillies.
<br />4).Stir well till they begin to crackle. Add the poppy seed paste and salt. Cook for 2 minutes.
<br />5).Now mix in the potatoes, pour a dash of water and leave it to simmer for 7-8 minutes.
<br />6).Garnish and serve.
