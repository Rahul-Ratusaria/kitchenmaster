<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Shukto</h1>
1 potato
<br />1 karela
<br />1/2 raw banana
<br />1 small sweet potato
<br />6 small bari
<br />100 gm french beans
<br />1 drum stick
<br />25 gm ginger paste
<br />25 gm mustard paste
<br />A pinch of chilli powder
<br />1/2 tsp turmeric
<br />10 gm panch phoran masala
<br />Salt to taste
<br />25 gm mustard oil
<br />1/2 cup water
</div>

<b><h1>How to Make Shukto</h1></b>
1). Cut and wash the vegetables and raw banana. Put the pieces into turmeric water.
<br />2). Heat mustard oil in a pan. Add panch foran masala and the ginger paste. Cook until it gets golden brown.
<br />3). Add mustard paste, chilli powder and turmeric. Cook until the oil separates.
<br />4). Then add the vegetables and saute for 2 minutes.
<br />5). Add water and cook until the vegetables are tender.
<br />6). The dish is ready to serve.
