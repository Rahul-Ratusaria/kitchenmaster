<h1>Ingredients Of Mutton Seekh Kebab</h1>
Oven temp: 375 F- 205 C
<br />2 cups keema (minced mutton/lamb/beef)
<br /><b>For marination:</b>
<br />1 Tbsp vinegar
<br />2 Tbsp chopped fenugreek leaves
<br />1/2 Tbsp garlic paste
<br />1/2 Tbsp ginger paste
<br />1 1/2 Tbsp salt
<br />1/4 Tbsp powdered black pepper
<br />1/4 Tbsp garam masala
<br />2 Tbsp chopped coriander leaves
<br />1 Tbsp finely chopped green chillies
<br />Skewers to make the kebabs
<br /><b>For garnishing:</b>
<br />Oil for brushing
<br />Chaat masala
<br />Lemon wedges for garnishing
</div>

<b><h1>How to Make Mutton Seekh Kebab</h1></b>
<b>For the marination:</b>
<br />1).In a large bowl, mix lamb mince with few Tbsp of vinegar, fenugreek leaves.
<br />2).Then add garlic paste, ginger paste, salt and black pepper as per taste.
<br />3).Add chopped coriander leaves and green chillies.
<br />4).Refrigerate the meat mixture, covered, to marinate for at least 5 hours.
<br /><b>For the kebabs:</b>
<br />1).About 25 minutes before serving, shape the meat mixture into long 'tubes' around the skewers (seekhs) and place on to a grill over a drip tray or into the pre-heated oven (also on a drip tray) and bake for 20-25 minutes.
<br />2).Brush them with oil and cook for another 2 minutes.
<br />3).Using oven mittens or a cloth, carefully push the kebab from one end, on to a serving dish.
<br />4).Garnish with the chaat masala, onions, lemons and serve along with green chutney.


