<h1>Ingredients Of Olan</h1>
Ash gourd (kumbalanga)-1 cup
<br />   Yellow pumpkin (Mathanga ) -1 cup
<br />   Green chilli -3-4 slit
<br />   Thin coconut milk -3/4 cup
<br />   Thick coconut milk -1/2 cup
<br />   Black eyed peas/vanpayar -1/4 cup
<br />   Salt needed
<br />   Coconut oil -3 tsp
<br />   Curry leaves -few
</div>
<b><h1>How to Make Olan</h1></b>
<h3><b>Preparation</b></h3>
Soak black eyed peas overnight and pressure cook with a little salt for 2 whistles. Keep it aside.
<br />Peel and chop both ash gourd and pumpkin into medium sized pieces.
<br />Extract coconut milk from half a coconut or use ready made coconut milk. If using store bought coconut milk, dilute it with water for thin coconut milk. Check out  how to extract coconut milk  in detail.
<br /><h3><b>Method</b></h3>
Cook both ash gourd and pumpkin along with slit green chillies and salt needed in thin coconut milk.
<br />Once it is cooked, add thick coconut milk, cooked cow peas (vanpayar) and cook over low flame for 5-6 minutes and switch off the heat. (Do not boil too much after adding 1 st or thick coconut milk)
<br />Add curry leaves and drizzle raw coconut oil on top.
<br />Yummy Kerala Olan is ready to be served.

