<b><h1>Ingredients Of Rogan Josh</h1></b>
<br />1 kg meat-cut into pieces
<br />5 cups water
<br />2 1/4 tsp salt
<br />1 Tbsp ground garlic
<br />1 cup pure ghee
<br />4 cloves
<br />8 green cardamoms
<br />5 tsp turmeric powder
<br />2 Tbsp onion paste-fried
<br />4 tsp Kashmiri red chilli powder-dissolved in a cup of water
<br />1/2 tsp saffron-ground and added to 2 tbsp warm water
<br />1 cup dry cockscomb (mawal) flowers-heated with a cup of water
<br />1/4 tsp black pepper powder
</div>

<b><h1>How to Make Rogan Josh</h1></b>
<br />1.Boil the meat in the water; remove the scum with a ladle until the water is clear.
<br />2.Add the salt and the garlic. Boil until the meat is half done.
<br />3.Remove from heat and take out the pieces of the meat. Wash them in a pan of cold water. Keep the meat aside. Then strain the water through a fine sieve and collect it in another pan.
<br />4.Return this pan to the heat and bring the water to a boil and add the meat. Meanwhile, heat the ghee in a pan; add the cloves, and saute until they crackle.
<br />5.Remove from heat and sprinkle a tbsp of water and cover.
<br />6.To the boiling water, add the green cardamoms, turmeric powder, clove-flavored ghee and the onion paste. Boil for another 10 minutes.
<br />7.Stir in the red chilli water. Reduce heat and cook covered until the meat is tender.
<br />8.Add the cockscomb flower extract, saffron water and black pepper powder.
<br />9.Mix well and bring rapidly to a boil.

