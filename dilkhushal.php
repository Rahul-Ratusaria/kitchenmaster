<h3 style="padding-top:20px; margin:10px; color:#0000cc;">This dil khushal is a typical Rajasthani Desert, available all across the India. But the origin of this sweet is Rajasthan. Here is the recipe for the sweet Dil Khushal.
</h2>
<div>

<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Dil Khushal</h1>
coarsely ground besan (Bengal gram flour,2 cups )
<br />ghee (1 cup )
<br />grated mava (khoya,1 cup/150 grams)
<br />cardamom powder (elaichi,1 tsp )
<br /><b>For The Sugar Syrup</b>
<br />sugar, 1 1/2 cups (300 grams)
<br />water 1 cup
<br />milk 2 tbsp
<br />For Garnish
<br />Chopped Almond and Pistas
</div>

<b><h1>How to Make Dil Khushal</h1></b>
1).Take the gram flour in a bowl.
<br />2).In a pan Heat the ghee and add half of it to the gram flour.
<br />3).Mix the ghee into the gram flour till the mixture become like bread crumbs.
<br />4).Put the remaining hot ghee in a pan, put the gram flour mixture and cook till the mixture turns golden brown, stir continuously.
<br />5).Now add the grated khoya and cardamom powder and stir for about 10 minutes.
<br />6).Take this away from the heat and keep aside to cool.
<br /><b>For the sugar syrup</b>
<br />Add enough sugar with water in a pan and simmer for 10 minutes, let it boil.
<br />Add 2 tablespoon of milk to the boiling sugar syrup.
<br />Now the impurities will from a gray layer. Take out this layer gently using a slotted spoon.
<br />Keep it on boil till the syrup is of 1 string consistency and keep it hot.
<br /><b>How to proceed</b>
<br />Now add the hot sugar syrup over the cooked gram flour mixture and mix well.
<br />Now pour into a greased surfaced thali.
<br />Add some chopped almonds and Pistas on top and allow to set for at least 5 hours.
<br />Cut into pieces of your choice.
<br />Serve and Enjoy.
