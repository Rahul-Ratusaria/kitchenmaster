<h2 style="padding-top:20px; margin:10px; color:#0000cc;">Delicious dal cooked in spices and generous amounts of ghee, butter, cream and curd. Direct from Punjab, this maa ki dal recipe won't disappoint you!</h2>

<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<b><h1>Ingredients of Maa Ki Dal</h1></b>
<b><br />For the dal:</b>
<br />1 cup black urad dal, soaked overnight
<br />3 cups water
<br />3-4 cloves garlic, smashed
<br />1 small piece smashed ginger
<br />Salt for taste
<b><br />For the tadka:</b>
<br />2 Tbsp ghee
<br />2 brown cardamom
<br />4-5 cloves
<br />1 stick cinnamon
<br />1/2 tsp cumin seeds
<br />1 onion, chopped
<br />1 tsp chopped ginger
<br />1 tsp chopped garlic
<br />2 green chilies, chopped
<br />1/2 tsp turmeric powder
<br />1/2 tsp cumin powder
<br />1 tsp coriander powder
<br />1 tsp red chili powder
<br />1 tomato, chopped
<br />250 gm curd
<br />2 Tbsp cream
<br />2 Tbsp butter
<br />Juice of 1/2 lime
<b><br />For garnishing:</b>
<br />Coriander, chopped
<br />1 green chilli, split
<br />1 tsp cream
<br />1 tsp butter
</div>

<h1><b>How to Make Kali Dal</b></h1>
<b><br />For the dal:</b>
<br />1.In a pressure cooker add a cup of overnight soaked dal with 3 cups of water. Pressure cook them for 45 minutes with little bit of salt, ginger and garlic.
<br />2.Transfer the pressure cooked dal into a kadhai. Cook it with about 2 cups of water on medium heat. Stir at intervals.
<b><br />For the tadka:</b>
<br />1.In a different pan add ghee, brown cardamom, cloves, cinnamon and cumin seeds.
<br />2.Saute them well and then add chopped onions, ginger, garlic and green chilies into it.
<br />3.Mix them well together and add turmeric powder, cumin powder, coriander powder, red chili powder and salt to taste.
<br />4.Roast them well together.
<br />5.Saute them with chopped tomato and little bit of water to de glaze. Once the tomatoes are cooked through add this mixture into the kadhai with the simmering dal.
<br />6.Now add curd, cream, butter and squeeze half a lime.
<br />7.Mix them and let it simmer for 10 minutes on low heat.
<br />8.Garnish with coriander, butter, green chillies and cream.
<br />9.Serve pipping hot!

