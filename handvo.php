<h3 style="padding-top:20px; margin:10px; color:#0000cc;">A traditional Gujarati savoury cake. It is made with a combinations of lentils, rice and buttermilk.
</h2>
<div>

<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Handvo</h1>
1 medium bottle gourd
<br />3 Tbsp coriander chopped
<br />3-4 green chillies finely chopped
<br />3 cups buttermilk
<br />3 tsp soda bi-carb
<br />1 tsp each urad and channa dal
<br />1 tsp each cumin and mustard seeds
<br />4-5 Tbsp oil
<br />salt to taste
<br /><b>For grinding together:</b>
<br />1 cup rice
<br />1 cup yellow moong dal

</div>

<b><h1>How to Make Handvo</h1></b>
1).Take butter milk in a large vessel.
<br />2).Add salt, soda and flour, mix well.
<br />3).Keep it aside for 6-7 hours.
<br />4).Grate bottle gourd and squeeze out excess water.
<br />5).Then add gourd, coriander and green chillies.
<br />6).Heat oil in a pan, add urad & channa dals, and seeds.
<br />7).Allow to splutter, pour half in the batter and mix it thoroughly.
<br />8).Put it in a greased oven proof deep container.
<br />9).Pour remaining seasoning on top.
<br />10).Place in a preheated oven at 280oC for 10 minutes.
<br />11).Reduce to 200 C or 180 C for 40 minutes or till done.
<br />12).Check by inserting a skewer, which should come out clean.
<br />13).Slice into wedges and serve hot.


