<h1>Ingredients Of Mughlai Pulao</h1>
750 Gram Chicken
<br />1 tsp Salt
<br />1 tsp Red Chilli Powder
<br />1 tsp Lemon Juice
<br />3 Tbsp Ghee
<br />2 Tbsp Cashews
<br />2 Tbsp Raisins
<br />2 Tbsp Pistachios
<br />2 Tbsp Almonds, blanched
<br />1/2 Cup Onions
<br />1 tsp Ginger Garlic Paste
<br />1 tsp Cumin Seeds
<br />1/2 tsp Fennel Seeds
<br />1 tsp Coriander Seeds
<br />2 Bay Leaves
<br />1 Cinnamon Stick
<br />5 Cloves
<br />5 Black Pepper Corn
<br />2 Black Cardamom
<br />5 Green Cardamom
<br />1/2 tsp Salt
<br />2 Cups Rice
<br />2 Cups Water
<br />1/2 tsp Saffron

</div>

<b><h1>How to Make Mughlai Pulao</h1></b>
1).Take the chicken in a bowl, add salt, red chilli powder and lemon juice.Mughlai Pulav.
<br />2).Mash it together with hands to mix the chicken completely with ingredients.Mughlai Pulav.
<br />3).Keep it aside for two hours to marinate.Mughlai Pulav.
<br />4).Now take a pan and put some ghee in to it. Add cashews, raisins, pistachios and almonds.Mughlai Pulav.
<br />5).Fry all the nuts together.
<br />6).Take another pan with oil and out the chicken pieces in to it.Mughlai Pulav.
<br />7).Fry the pieces thoroughly until golden brown.Mughlai Pulav.
<br />8).Now put onion in another pan of oil and fry until golden brown.Mughlai Pulav.
<br />9).Add ginger garlic paste to onion and mix.
<br />10).Now put cumin seeds, fennel seeds, coriander seeds.Mughlai Pulav.
<br />11).Saute them until golden brown and then put bay leaves, cinnamon stick, cloves, black pepper corn, black cardamom and green cardamom and salt.Mughlai Pulav.
<br />12).Saute them well.
<br />13).Now add Rice to the mix and saute well.Mughlai Pulav.
<br />14).Add water and saffron. Mix well.Mughlai Pulav.
<br />15).Now put the fried chicken to the pan and mix. Cover it for about 10 minutes.Mughlai Pulav.
<br />16).After 10 minutes put the fried nuts in to the pan and mix all the ingredients thoroughly.Mughlai Pulav.
<br />17).Serve hot.

