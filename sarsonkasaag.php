<h3 style="padding-top:20px; margin:10px; color:#0000cc;">Nothing is more quintessentially Punjabi than sarson da saag paired with makki di roti. A stew-like preparation made with mustard greens.
</h2>
<div>

<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Sarson Ka Saag</h1>
750 gm sarson da saag/ mustard leaf (roughly chop)
<br />250 gm spinach (roughly chop)
<br />100 gm moolipatta / white radish leaf (roughly chop)
<br />30 gm mathua (roughly chop)
<br />30 gm ginger (diced)
<br />8 green chilli (slit & deseeded)
<br />45 gm rice
<br />60 ml mustard oil
<br />15 gm makki ka atta / maize flour
<br />200 gm white butter
<br />100 gm jaggery / gur
<br />1 nasturtium (edible flower)
<br />Salt to taste
</div>

<b><h1>How to Make Sarson Ka Saag</h1></b>
1).Put all the ingredients, except maize flour, gur and white butter, in a handi/pan.
<br />2).Add water 2 litres of water, bring to a boil, reduce to low heat and simmer until the greens are tender for about 45 minutes.
<br />3).Remove handi/pan from heat and churn with a wooden churner.
<br />4).Return handi/pan to heat, add maize flour, cover and simmer over very low heat, stirring at regular intervals, for 1 hour.
<br />5).Remove and adjust the seasoning.
<br />6).Pour in a bowl, and add large dollops of butter.
<br />7).Add a few petals of edible flower and serve with gur and Makki ki Roti.

