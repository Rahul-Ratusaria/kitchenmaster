<h1>Ingredients Of Ayila Porichathu</h1>
<br />4 Fish, ayala (bangada fish)
<br />1-1/2 tablespoon Red chilli powder
<br />1/4 teaspoon Turmeric powder (Haldi)
<br />1/2 teaspoon Fennel Powder
<br />1 teaspoon Black pepper powder
<br />1 teaspoon Ginger Garlic Paste
<br />1 teaspoon Lemon juice
<br />Salt, to taste
<br />Cooking oil, for shallow fry
</div>

<b><h1>How to Make Ayila Porichathu</h1></b>
1).To begin making the Ayala Fish Fry recipe, wash and clean fish well. Leave it whole. Score the fish on itï¿½s skin on both sides.
<br />2).In a bowl squeeze lime juice. Add all the spice powders, ginger-garlic paste, salt and mix well. Make a thick paste.
<br />3).Rub the spice paste well all over the fish including the slit areas on both sides.
<br />4).Cover and let the fish marinate in fridge for 15 minutes.
<br />5).Heat few tablespoons oil in a pan. Shallow fry the whole fish on both sides until golden brown on medium flame.
<br />6).Serve hot with lime wedges and onion carrot slices if preferred.
<br />7).Ayala Fish Fry pairs very well with biryani, sambar rice, rasam rice or just like that as a starter. For a meal, serve it with Mixed Vegetable Sambar, Steamed Rice and Elai Vadam for a weekday lunch or dinner.
