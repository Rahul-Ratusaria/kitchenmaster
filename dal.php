<h3 style="padding-top:20px; margin:10px; color:#0000cc;">'Daal Dhokli' is a popular Gujarati curry. Savory dumplings made of whole wheat flour simmered in tuvar daal.
</h2>
<div>

<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Dal Dhokli</h1>
<b>For the dal:</b>
<br />1 cup tuvar dal
<br />2 Tbsp peanuts-soaked in water for 30 minutes
<br />5 cloves garlic-chopped
<br />4 kokums
<br />1 tsp cumin seeds
<br />1/2 tsp turmeric powder
<br />1/2 tsp chilli powder
<br /><b>For the dhokli:</b>
<br />1 cup wheat flour (aata)
<br />1/2 tsp asafoetida
<br />1/2 tsp chilli powder
<br />1/2 tsp turmeric
<br />Oil
<br />Salt to taste

</div>

<b><h1>How to Make Dal Dhokli</h1></b>
1). Boil daal and the peanuts in a pressure cooker till soft. Keep aside.
<br />2). Heat 1 Tbsp ghee in a pan and add cumin seeds, garlic, asafoetida and finally, the boiled daal.
<br />3). Add cocum, turmeric powder, chilli powder, sugar and curry leaves. Boil and add salt to taste.
<br />4). Note: You can add vegetables also.
<br />5). To make the dhokli, mix the flour, salt, turmeric, chilli powder, asafoetida and a little oil and make a stiff dough with water. Knead well and roll out into 10 cm discs.
<br />6). Cut the discs into diamonds or squares with a knife and drop them into the boiling daal mixture.
<br />7). When all the dhokli pieces are put in, boil the daal for a further 15 minutes on a low fire. Garnish with coriander leaves and serve hot.


