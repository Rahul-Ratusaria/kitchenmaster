<div style="background-color:lightgrey; font-size:20px; float:left; padding-left:30px; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<b><h1>Ingredients Of Tandoori Chicken</h1></b>
<br />8 Large Chicken pieces with skin (2 pieces breast, 2 pieces of thighs, a couple of wings)
<br />To taste Oil
<br /><b><h1>For the marinade:</h1></b>
<br />4 tsp Red chilli paste
<br />3 Tbsp Ginger and garlic paste
<br />2 tsp Chaat masala
<br />1 1/2 tsp Tandoori Masala
<br />1 Tbsp Oil
<br />3 Tbsp Curd
<br /><b><h1>To taste Salt</h1></b>
<br />1/2 Lemon (juiced)
<br /><b><h1>For the tandoori masala:</h1></b>
<br />2 Sticks Cinnamon
<br />1 Tbsp Black peppercorns
<br />5 Green cardamom
<br />3 Brown cardamom
<br />2 tsp Coriander seeds
<br />2 tsp Cumin seeds
<br />3 Cloves
<br />1 Bay leaf
<br />3/4 tsp Turmeric
</div>


<h1><b>How to Make Tandoori Chicken</b></h1>
<b>1.Prepare the marinade:</b>
<br /><br />1.In a bowl add ginger and garlic paste, red chilli paste, chaat masala, tandoori masala, oil, curd, salt and lemon juice. Mix it well.
<br />2.Rub the marinade on the chicken pieces, over the skin and underneath it. Make small gashes in the chicken pieces for the marinade to coat it nicely.
<br />3.Marinate the chicken pieces for 30 minutes.
<br />4.Char grill the chicken pieces on a griller or tandoor. Baste with oil at regular intervals.5.Serve with lemon wedges and onion rings.
<br /><br /><b>Prepare tandoori masala:</b>
<br /><br />1.In a pan dry roast cinnamon, peppercorns, green cardamom, brown cardamom, coriander seeds, cumin seeds, cloves, bay leaf and turmeric powder.
<br />2.In a mortar and pestle grind the dry roasted spices into a fine powder. Masala is ready.
<br />3.Tip: To make sure the chicken does not stick to the grill, glaze the grill with some oil.
<br />4.If you are making it in an oven, the temperature needs to be around 250-300 degrees C.
<br />5.Serve hot.
