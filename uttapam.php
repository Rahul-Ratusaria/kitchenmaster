<h3 style="padding-top:20px; margin:10px; color:#0000cc;">A traditional delicacy from down South which literally means 'poured appam' in Tamil. Uttapam is a dosa-like preparation made with the batter of rice, dhuli urad dal and fenugreek seeds.
</h2>
<div>

<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Uttapam</h1>
360 Gram Rice (parboiled)
<br />90 Gram Bengal Gram (dhuli urad) (split and husked)
<br />1/2 tsp Fenugreek seeds
<br />2 tsp Salt
<br />Iron griddle or tawa
<br />To smear the pan Oil
</div>

<b><h1>How to Make Uttapam</h1></b>
1).Soak rice, daal and methi in water for 5-6 hours.
<br />2).Grind fine, add salt and enough water to make a dropping consistency and leave to ferment 5-6 hours.
<br />3).Heat tawa, and brush oil over it. When really hot, splash a little water over it, and pour about 1 cup of batter on to it.
<br />4).It will spread a little. When the edges start browning a bit, pour a trail of oil around it.
<br />5).Turn it over and let it brown on the other side too.
<br /><b><h2>Prepare onion uttapam:</h2></b>
1).Before you turn the uttapam over, sprinkle a mixture of finely chopped onions, tomatoes, coriander leaves and green chillies.
<br />2).Serve hot.

