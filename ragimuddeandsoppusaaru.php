<h3 style="padding-top:20px; margin:10px; color:#0000cc;">Massoppu or masoppu saaru and ragi mudde is a very popular combination in southern parts of Karnataka. Massoppu saaru is a gravy prepared using mashed greens and toor dal. Ragi mudde or ragi ball is a soft ball prepared using finger millet flour. This popular combination is consumed as a wholesome meal.
</h2>
<div>

<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Ragi Mudde and Soppu Saaru</h1>
1 bunch green leaves
<br />1.5 cup toor dal
<br />1 finely chopped onion
<br />1 finely chopped tomato
<br />2 tbsp grated coconut (optional)
<br />A big pinch of turmeric powder
<br />2 - 4 green chilli
<br />Small gooseberry sized tamarind
<br />1/2 tsp roasted cumin powder or rasam powder
<br />1 tbsp chopped coriander leaves
<br />Salt as per your taste
<br /><b>For tempering:</b>
<br />1 red chilli
<br />1/4 tsp mustard seeds
<br />1/4 tsp cumin seeds
<br />4 -5 cloves of chopped garlic
<br />A big pinch of asafoetida
<br />4 - 5 curry leaves
<br />2 tsp cooking oil
</div>

<b><h1>How to Make Ragi Mudde and Soppu Saaru</h1></b>
1).Clean, wash and finely chop the green leaves. green leaves for massoppu saaru
<br />2).In a pressure cooker rinse and cook toor dal by adding a pinch of turmeric powder and required water.cook toor dal for massoppu saaru
<br />3).Now to the same cooker, which has cooked toor dal, add in chopped green leaves, onion, tomato, grated coconut and slit green chillis. Add in a cup of water, close the lid and cook it by making a whistle.cook dal and leaves for massoppu saaru
<br />4).Once the pressure subsidies, open the lid and mash the cooked ingredients well. Mash it with hand churner or blender. You can also drain the liquid and coarse grind the residue using mixer grinder. I usually mash it well with the back of a spatula. mash the leaves for massoppu saaru
<br />5).Now take a small frying pan and prepare tempering using oil, red chilli, mustard seeds, cumin seeds, garlic, asafoetida and curry leaves.tempering for massoppu saaru
<br />6).Add in tempering to the cooker, which has mashed dal, leaves and vegeables. Also add in salt and tamarind juice extracted from a small gooseberry sized tamarind.salt and tamarind for massoppu saaru
<br />7).Add in 1/2 tsp of roasted cumin powder or rasam powder and bring it to boil.cumin or rasam powder for massoppu saaru
<br />8).Add in chopped coriander leaves. When it comes to rolling boil switch off the stove. Serve it hot with ragi mudde.
