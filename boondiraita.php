<div style="background-color:lightgrey; font-size:20px; padding-left:30px; float:left; padding-bottom:20px; margin-right:20px; width:300px; border-radius:20px;">
<h1>Ingredients Of Boondi Raita</h1>
    500 gm gram flour
<br />    1 liter water or milk
<br />    750gm ghee for frying
<br />    750 gm sugar
<br />    3 1/2 cups water
<br />    10-12 drops orange color
<br />    10-12 flakes saffron - soaked in little water
<br />    50 gm chopped cashew nuts
<br />    50 gm raisins
<br />    10 cardamoms - peeled
<br />    Boondi strainer or fryer of medium sized holes
</div>

<b><h1>How to Make Boondi Raita</h1></b>
1).    Prepare a thin batter with gram flour and water or milk. Heat ghee in a pan.
<br />2).    Fill the fryer or strainer with the batter up to the half.
<br />3).    Position it over pan and drain boondis in hot ghee by hitting the strainer on side of the pan, lifting up then again hitting. This process should be finished very quickly.
<br />4).    Fry them to golden color, drain and remove. Use up all the batter.
<br />5).    Prepare sugar syrup of one and a half thread consistency by boiling sugar and water.
<br />6).    Add saffron water and color to the syrup. Mix the boondis, syrup, dry fruits and cardamom.
<br />7).    After 10 minutes, sprinkle over a little hot water, cover and keep for 1 1/2 hour.
<br />8).    Prepare round balls with moist hands.


